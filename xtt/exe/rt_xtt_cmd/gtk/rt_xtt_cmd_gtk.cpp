XttCmdGtk::XttCmdGtk(GtkWidget* w)
{
  pwr_tStatus sts;
  pwr_tOName opplace = "";

  xnav = new XNavGtk(this, (GtkWidget*)w, "Plant", &brow_widget,
      (xnav_sStartMenu*)root_menu, opplace, 0, &sts);

  xnav->message_cb = &message_cb;
  xnav->close_cb = XttCmd::close_cb;
  xnav->login();
}

static int timeout_func()
{
  while (gtk_events_pending())
    gtk_main_iteration();
  return 1;
}

int main(int argc, char* argv[])
{
  pwr_tStatus sts;
  int i;
  char str[256];
  XttCmdGtk* cmd;
  int quiet = 0;
  int hide = 0;
  const int window_width = 400;
  const int window_height = 700;
  GtkWidget* toplevel;
  char title[80];

  /* If arguments, treat them as a command and then exit */
  // Open directory volume as default

  str[0] = 0;
  for (i = 1; i < argc; i++) {
    if (argv[i][0] == '-') {
      switch (argv[i][1]) {
      case 'h':
        XttCmd::usage();
        exit(0);
      case 'q':
        // Quiet
        quiet = 1;
        break;
      case 'i':
        // Hide
        hide = 1;
        break;
      case '-':
        // Display sent to gtk
        i++;
        break;
      default:
        std::cout << "Unknown argument: " << argv[i] << "\n";
      }
    } else {
      if (str[0] != 0)
        strcat(str, " ");
      strcat(str, argv[i]);
    }
  }

  sts = gdh_Init("rt_xtt_cmd");
  if (EVEN(sts)) {
    printf("Unable to connect to database\n");
    exit(0);
  }

  gtk_init(&argc, &argv);

  setlocale(LC_TIME, "en_US");
  setlocale(LC_NUMERIC, "POSIX");

  strcpy(title, "Xtt Cmd");

  // Gtk
  toplevel = (GtkWidget*)g_object_new(GTK_TYPE_WINDOW, "default-height",
      window_height, "default-width", window_width, "title", title, NULL);
  cmd = new XttCmdGtk(toplevel);

  gtk_container_add(GTK_CONTAINER(toplevel), cmd->brow_widget);
  gtk_widget_show_all(toplevel);
  if (hide)
    g_object_set(toplevel, "visible", FALSE, NULL);

  if (!quiet)
    std::cout << "\n\
Proview is free software; covered by the GNU General Public License.\n\
You can redistribute it and/or modify it under the terms of this license.\n\
\n\
Proview is distributed in the hope that it will be useful \n\
but WITHOUT ANY WARRANTY; without even the implied warranty of \n\
MERCHANTABILITY or FITNESS FOR A PARTICULAR PURPOSE. See the \n\
GNU General Public License for more details.\n\n";

  if (str[0] != 0) {
    str_trim(str, str);
    sts = cmd->xnav->command(str);
    if (ODD(sts))
      return 0;
    exit(sts);
  }
  sts = dcli_input_init(&cmd->chn, &cmd->recall_buf);
  if (EVEN(sts))
    exit(sts);

  // Init input

  while (1) {
    unsigned long terminator;
    unsigned long option = DCLI_OPT_TIMEOUT;

    /* get and parse the command */

    /* get input */
    dcli_qio_set_attr(&cmd->chn, 1);
    sts = dcli_get_input_string(&cmd->chn, str, &terminator, sizeof(str),
        cmd->recall_buf, option, 1, timeout_func, 0, "xttc> ");
    dcli_qio_reset(&cmd->chn);

    //    sts = scanf( "%s", str);

    if (streq(str, ""))
      continue;

    str_trim(str, str);
    sts = cmd->xnav->command(str);

    while (gtk_events_pending())
      gtk_main_iteration();
  }
}
