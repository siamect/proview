/* wb_ge_qt.cpp -- graphical editor */

#include <locale.h>

#include "cow_msgwindow_qt.h"
#include "cow_qt_helpers.h"
#include "cow_xhelp_qt.h"

#include "ge_qt.h"

#include "wb_log_qt.h"

#include <QApplication>

#define wnav_cInitFile "$pwrp_login/wtt_init"

/*  Fallback resources  */

static QWidget* toplevel;
static QWidget* mainwindow;

static void usage()
{
  printf("\nUsage: wb_ge [-l language] [graphname]\n");
}

int main(int argc, char* argv[])
{
  int i;
  pwr_tFileName file;
  char graph_name[80];
  int sts;
  unsigned int opt = ge_mOption_EnableComment;
  Ge* gectx;
  pwr_tFileName fname;

  QApplication app(argc, argv);

  setlocale(LC_NUMERIC, "POSIX");
  setlocale(LC_TIME, "en_US");

  toplevel = new QWidget();
  toplevel->setMinimumSize(100, 100);
  toplevel->setWindowTitle("Pwr wb_ge");
  toplevel->setAttribute(Qt::WA_DeleteOnClose);

  // Create help window
  CoXHelpQt* xhelp = new CoXHelpQt(mainwindow, 0, xhelp_eUtility_Wtt, &sts);
  CoXHelpQt::set_default(xhelp);

  debug_print("wb_ge_qt: started with arguments:\n");

  if (argc > 1) {
    for (i = 1; i < argc; i++) {
      debug_print("arg%d: %s\n", i, argv[i]);
      if (strcmp(argv[i], "-l") == 0) {
        if (i + 1 >= argc) {
          usage();
          exit(0);
        }
        Lng::set(argv[i + 1]);
        i++;
      } else {
        strcpy(file, argv[i]);
      }
    }

    if (file[0] == '@') {
      // Execute script
      pwr_tStatus sts;

      gectx = new GeQt(NULL, toplevel, 0, 1, 0, NULL);
      sts = gectx->command(file);
      if (EVEN(sts)) {
        gectx->message(sts);
      }
    } else {
      // Open graph
      strcpy(graph_name, file);
      gectx = new GeQt(NULL, mainwindow, 0, 1, opt, graph_name);

      sprintf(fname, "@%s.pwr_com", wnav_cInitFile);
      gectx->command(fname);
    }
  } else {
    gectx = new GeQt(NULL, mainwindow, 0, 1, opt, NULL);

    sprintf(fname, "@%s.pwr_com", wnav_cInitFile);
    gectx->command(fname);
  }

  toplevel->show();

  new wb_log_qt(toplevel);

  // Create message window
  MsgWindowQt* msg_window
      = new MsgWindowQt(gectx, mainwindow, "Ge messages", &sts);
  msg_window->find_ge_cb = Ge::find_ge_cb;
  MsgWindow::set_default(msg_window);

  return app.exec();
}