void GrowNode::set_rotation(
    double angel, double x0, double y0, glow_eRotationPoint type)
{
  GlowTransform t;

  if (fabs(angel - trf.rotation + trf.s.rotation) < FLT_EPSILON)
    return;

  switch (type) {
  case glow_eRotationPoint_LowerLeft:
    x0 = x_left;
    y0 = y_low;
    break;
  case glow_eRotationPoint_LowerRight:
    x0 = x_right;
    y0 = y_low;
    break;
  case glow_eRotationPoint_UpperRight:
    x0 = x_right;
    y0 = y_high;
    break;
  case glow_eRotationPoint_UpperLeft:
    x0 = x_left;
    y0 = y_high;
    break;
  case glow_eRotationPoint_Center:
    x0 = (x_left + x_right) / 2;
    y0 = (y_high + y_low) / 2;
    break;
  case glow_eRotationPoint_Zero:
    x0 = trf.s.a13;
    y0 = trf.s.a23;
    break;
  default:;
  }

  t.rotate(angel, x0, y0);
  trf.set(t * trf.s);
  get_node_borders();
  call_redraw_node_cons();
  ctx->set_dirty();
}

void GrowNode::draw(DrawWind* w, int ll_x, int ll_y, int ur_x, int ur_y)
{
  int tmp;

  if (ll_x > ur_x) {
    /* Shift */
    tmp = ll_x;
    ll_x = ur_x;
    ur_x = tmp;
  }
  if (ll_y > ur_y) {
    /* Shift */
    tmp = ll_y;
    ll_y = ur_y;
    ur_y = tmp;
  }

  if (x_right * w->zoom_factor_x - w->offset_x + 1 >= ll_x
      && x_left * w->zoom_factor_x - w->offset_x <= ur_x
      && y_high * w->zoom_factor_y - w->offset_y + 1 >= ll_y
      && y_low * w->zoom_factor_y - w->offset_y <= ur_y) {
    if (annot_scrollingtext != -1) {
      ctx->gdraw->set_clip_rectangle(w, x_left * w->zoom_factor_x - w->offset_x,
          y_low * w->zoom_factor_y - w->offset_y,
          x_right * w->zoom_factor_x - w->offset_x + 1,
          y_high * w->zoom_factor_y - w->offset_y + 2);
        }

    draw(w, (GlowTransform*)NULL, highlight, hot, (void*)this, NULL);
    if (annot_scrollingtext != -1) {
      ctx->gdraw->reset_clip_rectangle(w);
    }
  }
}

void GrowNode::draw(DrawWind* w, int* ll_x, int* ll_y, int* ur_x, int* ur_y)
{
  int tmp;
  int obj_ur_x = int(x_right * w->zoom_factor_x) - w->offset_x;
  int obj_ll_x = int(x_left * w->zoom_factor_x) - w->offset_x;
  int obj_ur_y = int(y_high * w->zoom_factor_y) - w->offset_y;
  int obj_ll_y = int(y_low * w->zoom_factor_y) - w->offset_y;

  if (*ll_x > *ur_x) {
    /* Shift */
    tmp = *ll_x;
    *ll_x = *ur_x;
    *ur_x = tmp;
  }
  if (*ll_y > *ur_y) {
    /* Shift */
    tmp = *ll_y;
    *ll_y = *ur_y;
    *ur_y = tmp;
  }

  if (obj_ur_x >= *ll_x && obj_ll_x <= *ur_x && obj_ur_y >= *ll_y
      && obj_ll_y <= *ur_y) {
    draw(w, (GlowTransform*)NULL, highlight, hot, (void*)this, NULL);

    // Increase the redraw area
    if (obj_ur_x > *ur_x)
      *ur_x = obj_ur_x;
    if (obj_ur_y > *ur_y)
      *ur_y = obj_ur_y;
    if (obj_ll_x < *ll_x)
      *ll_x = obj_ll_x;
    if (obj_ll_y < *ll_y)
      *ll_y = obj_ll_y;
  }
}

void GrowNode::draw(DrawWind* w, GlowTransform* t, int highlight, int hot,
    void* node, void* colornode)
{
  hot = (w == ctx->navw) ? 0 : hot;
  if (node && !root_node && node != (void*)this)
    root_node = (GrowNode*)node;

  if (!node)
    node = (void*)this;
  if (!colornode)
    colornode = (void*)this;
  else if (((GrowNode*)colornode)->fill_drawtype == glow_eDrawType_No
      && ((GrowNode*)colornode)->color_tone == glow_eDrawTone_No)
    colornode = (void*)this;

  if (((GrowNode*)node)->invisible || invisible)
    return;

  if (input_focus) {
    switch (nc->get_base_nc()->input_focus_mark) {
    case glow_eInputFocusMark_Relief: {
      hot = 1;

      glow_sPoint p1 = {x_left, y_low};
      glow_sPoint p2 = {x_right, y_high};
      if (t) {
        p1 = *t * p1;
        p2 = *t * p2;
      }

      p1.x = p1.x * w->zoom_factor_x - w->offset_x - 2;
      p1.y = p1.y * w->zoom_factor_y - w->offset_y - 2;
      p2.x = p2.x * w->zoom_factor_x - w->offset_x + 2;
      p2.y = p2.y * w->zoom_factor_y - w->offset_y + 2;

      ctx->gdraw->line((int)p1.x, (int)p1.y, (int)p1.x, (int)p2.y,
          glow_eDrawType_DarkGray, 0, 0);
      ctx->gdraw->line((int)p1.x, (int)p1.y, (int)p2.x, (int)p1.y,
          glow_eDrawType_DarkGray, 0, 0);
      ctx->gdraw->line((int)p2.x, (int)p1.y, (int)p2.x, (int)p2.y,
          glow_eDrawType_LightGray, 0, 0);
      ctx->gdraw->line((int)p1.x, (int)p2.y, (int)p2.x, (int)p2.y,
          glow_eDrawType_LightGray, 0, 0);
      break;
    }
    default:;
    }
  }

  // If group member with click action, set hot
  if (ctx->trace_started && this->hot && root_node &&
      ((root_node->type() == glow_eObjectType_GrowGroup && is_sensitive()) ||
       root_node->type() == glow_eObjectType_GrowToolbar) &&
      !((GrowGroup *) root_node)->hot)
    hot = 1;

  if (feq(fill_level, 1.0)) {
    if (t) {
      GlowTransform trf_tot;
      trf_tot.set(*t * trf);

      if (!trace.p)
        nc->draw(w, &trf_tot, highlight, hot, (void*)this, colornode);
      else
        // If this node has a trace pointer, use colors for this node
        nc->draw(w, &trf_tot, highlight, hot, (void*)this, (void*)this);
    } else
      nc->draw(w, &trf, highlight, hot, node, node);
  } else {
    double x_level = 0, y_level = 0;
    int clip_sts = 0;
    glow_eDrawTone old_color_tone = glow_eDrawTone_No;
    glow_eDrawType old_fill_drawtype = glow_eDrawType_No;

    Matrix tmp = t ? (*t * trf) : trf;
    glow_sPoint p1 = tmp * glow_sPoint({x_left, y_low});
    glow_sPoint p2 = tmp * glow_sPoint({x_right, y_high});

    p1.x = p1.x * w->zoom_factor_x - w->offset_x;
    p1.y = p1.y * w->zoom_factor_y - w->offset_y;
    p2.x = p2.x * w->zoom_factor_x - w->offset_x;
    p2.y = p2.y * w->zoom_factor_y - w->offset_y;

    switch (level_direction) {
    case glow_eDirection_Right:
      x_level = fill_level * (p2.x - p1.x);
      clip_sts = ctx->gdraw->set_clip_rectangle(
          w, (int)p1.x - 1, (int)p1.y - 1, (int)(p1.x + x_level), (int)p2.y + 1);
      break;
    case glow_eDirection_Left:
      x_level = (1 - fill_level) * (p2.x - p1.x);
      clip_sts = ctx->gdraw->set_clip_rectangle(
          w, (int)p1.x - 1, (int)p1.y - 1, (int)(p1.x + x_level), (int)p2.y + 1);
      if (level_color_tone != glow_eDrawTone_No) {
        old_color_tone = color_tone;
        color_tone = level_color_tone;
      } else if (level_fill_drawtype != glow_eDrawType_No) {
        old_fill_drawtype = fill_drawtype;
        fill_drawtype = level_fill_drawtype;
      }
      break;
    case glow_eDirection_Up:
      y_level = fill_level * (p2.y - p1.y);
      clip_sts = ctx->gdraw->set_clip_rectangle(
          w, (int)p1.x - 1, (int)p1.y - 1, (int)p2.x + 1, (int)(p1.y + y_level));
      break;
    case glow_eDirection_Down:
      y_level = (1 - fill_level) * (p2.y - p1.y);
      clip_sts = ctx->gdraw->set_clip_rectangle(
          w, (int)p1.x - 1, (int)p1.y - 1, (int)p2.x + 1, (int)(p1.y + y_level));
      if (level_color_tone != glow_eDrawTone_No) {
        old_color_tone = color_tone;
        color_tone = level_color_tone;
      } else if (level_fill_drawtype != glow_eDrawType_No) {
        old_fill_drawtype = fill_drawtype;
        fill_drawtype = level_fill_drawtype;
      }
      break;
    default:;
    }

    if (t) {
      GlowTransform trf_tot;
      trf_tot.set(*t * trf);

      if (!trace.p)
        nc->draw(w, &trf_tot, highlight, hot, (void*)this, colornode);
      else
        // If this node has a trace pointer, use colors for this node
        nc->draw(w, &trf_tot, highlight, hot, (void*)this, (void*)this);
    } else
      nc->draw(w, &trf, highlight, hot, node, node);
    if (ODD(clip_sts)) {
      ctx->gdraw->reset_clip_rectangle(w);
    }

    switch (level_direction) {
    case glow_eDirection_Right:
      if (level_color_tone != glow_eDrawTone_No) {
        old_color_tone = color_tone;
        color_tone = level_color_tone;
      } else if (level_fill_drawtype != glow_eDrawType_No) {
        old_fill_drawtype = fill_drawtype;
        fill_drawtype = level_fill_drawtype;
      }
      clip_sts = ctx->gdraw->set_clip_rectangle(
          w, (int)(p1.x + x_level), (int)p1.y - 1, (int)p2.x + 1, (int)p2.y + 1);
      break;
    case glow_eDirection_Left:
      if (level_color_tone != glow_eDrawTone_No)
        color_tone = old_color_tone;
      else if (level_fill_drawtype != glow_eDrawType_No)
        fill_drawtype = old_fill_drawtype;
      clip_sts = ctx->gdraw->set_clip_rectangle(
          w, (int)(p1.x + x_level), (int)p1.y - 1, (int)p2.x + 1, (int)p2.y + 1);
      break;
    case glow_eDirection_Up:
      if (level_color_tone != glow_eDrawTone_No) {
        old_color_tone = color_tone;
        color_tone = level_color_tone;
      } else if (level_fill_drawtype != glow_eDrawType_No) {
        old_fill_drawtype = fill_drawtype;
        fill_drawtype = level_fill_drawtype;
      }
      clip_sts = ctx->gdraw->set_clip_rectangle(
          w, (int)p1.x - 1, (int)(p1.y + y_level), (int)p2.x + 1, (int)p2.y + 1);
      break;
    case glow_eDirection_Down:
      if (level_color_tone != glow_eDrawTone_No)
        color_tone = old_color_tone;
      else if (level_fill_drawtype != glow_eDrawType_No)
        fill_drawtype = old_fill_drawtype;
      clip_sts = ctx->gdraw->set_clip_rectangle(
          w, (int)p1.x - 1, (int)(p1.y + y_level), (int)p2.x + 1, (int)p2.y + 1);
      break;
    default:;
    }

    if (t) {
      GlowTransform trf_tot;
      trf_tot.set(*t * trf);

      if (!trace.p)
        nc->draw(w, &trf_tot, highlight, hot, (void*)this, colornode);
      else
        // If this node has a trace pointer, use colors for this node
        nc->draw(w, &trf_tot, highlight, hot, (void*)this, (void*)this);
    } else
      nc->draw(w, &trf, highlight, hot, node, node);
    if (ODD(clip_sts)) {
      ctx->gdraw->reset_clip_rectangle(w);
    }

    switch (level_direction) {
    case glow_eDirection_Right:
      if (level_color_tone != glow_eDrawTone_No)
        color_tone = old_color_tone;
      else if (level_fill_drawtype != glow_eDrawType_No)
        fill_drawtype = old_fill_drawtype;
      break;
    case glow_eDirection_Left:
      break;
    case glow_eDirection_Up:
      if (level_color_tone != glow_eDrawTone_No)
        color_tone = old_color_tone;
      else if (level_fill_drawtype != glow_eDrawType_No)
        fill_drawtype = old_fill_drawtype;
      break;
    case glow_eDirection_Down:
      break;
    default:;
    }
  }
}

