void GlowArc::save(std::ofstream& fp, glow_eSaveMode mode)
{
  fp << int(glow_eSave_Arc) << '\n';
  fp << int(glow_eSave_Arc_draw_type) << FSPACE << int(draw_type) << '\n';
  fp << int(glow_eSave_Arc_line_width) << FSPACE << line_width << '\n';
  fp << int(glow_eSave_Arc_angle1) << FSPACE << angle1 << '\n';
  fp << int(glow_eSave_Arc_angle2) << FSPACE << angle2 << '\n';
  fp << int(glow_eSave_Arc_fill) << FSPACE << fill << '\n';
  fp << int(glow_eSave_Arc_ll) << '\n';
  ll.save(fp, mode);
  fp << int(glow_eSave_Arc_ur) << '\n';
  ur.save(fp, mode);
  fp << int(glow_eSave_End) << '\n';
}

void GlowArc::open(std::ifstream& fp)
{
  int type = 0;
  int end_found = 0;
  char dummy[40];
  int tmp;

  for (;;) {
    if (!fp.good()) {
      fp.clear();
      fp.getline(dummy, sizeof(dummy));
      printf("** Read error GlowArc: \"%d %s\"\n", type, dummy);
    }

    fp >> type;
    switch (type) {
    case glow_eSave_Arc:
      break;
    case glow_eSave_Arc_draw_type:
      fp >> tmp;
      draw_type = (glow_eDrawType)tmp;
      break;
    case glow_eSave_Arc_line_width:
      fp >> line_width;
      break;
    case glow_eSave_Arc_angle1:
      fp >> angle1;
      break;
    case glow_eSave_Arc_angle2:
      fp >> angle2;
      break;
    case glow_eSave_Arc_fill:
      fp >> fill;
      break;
    case glow_eSave_Arc_ll:
      ll.open(fp);
      break;
    case glow_eSave_Arc_ur:
      ur.open(fp);
      break;
    case glow_eSave_End:
      end_found = 1;
      break;
    default:
      std::cout << "GlowArc:open syntax error\n";
      fp.getline(dummy, sizeof(dummy));
    }
    if (end_found)
      break;
  }
}

void GlowArc::draw(DrawWind *w, void* pos, int highlight, int hot, void* node)
{
  int ll_x, ll_y, ur_x, ur_y;
  if (w == ctx->navw) {
    hot = 0;
    ll_x = ll.nav_z_x;
    ll_y = ll.nav_z_y;
    ur_x = ur.nav_z_x;
    ur_y = ur.nav_z_y;
  } else {
    ll_x = ll.z_x;
    ll_y = ll.z_y;
    ur_x = ur.z_x;
    ur_y = ur.z_y;
  }
  int idx = int(w->zoom_factor_y / w->base_zoom_factor * line_width - 1);
  idx += hot;
  idx = MAX(0, idx);
  idx = MIN(idx, DRAW_TYPE_SIZE - 1);
  GlowPoint* p = (GlowPoint*)pos;
  if (!fill) {
    ctx->gdraw->arc(ll_x + p->z_x - w->offset_x, ll_y + p->z_y - w->offset_y,
        ur_x - ll_x, ur_y - ll_y, angle1, angle2, draw_type, 0, idx, highlight);
  } else
    ctx->gdraw->arc(ll_x + p->z_x - w->offset_x, ll_y + p->z_y - w->offset_y,
        ur_x - ll_x, ur_y - ll_y, angle1, angle2, draw_type, 1, 0, highlight);
}

void GlowArc::draw_shadow(DrawWind *w, int border, int shadow, int highlight, int hot)
{
  int ll_x, ll_y, ur_x, ur_y;
  if (w == ctx->navw) {
    hot = 0;
    ll_x = ll.nav_z_x;
    ll_y = ll.nav_z_y;
    ur_x = ur.nav_z_x;
    ur_y = ur.nav_z_y;
  } else {
    ll_x = ll.z_x;
    ll_y = ll.z_y;
    ur_x = ur.z_x;
    ur_y = ur.z_y;
  }
  int idx = int(w->zoom_factor_y / w->base_zoom_factor * line_width - 1);
  idx += hot;
  idx = MAX(0, idx);
  idx = MIN(idx, DRAW_TYPE_SIZE - 1);
  int ish = 1;
  int offs = border;

  if (shadow && idx > 2) {
    if (angle1 == 0) {
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - idx + offs,
          ll_y - w->offset_y + idx / 2 - idx + offs,
          ur_x - ll_x + idx - offs * 2, ur_y - ll_y + idx - offs * 2,
          angle1 + 45, angle2 - 45, ctx->shift_drawtype(draw_type, -2, 0), 0,
          ish - 1, highlight);
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - idx + offs,
          ll_y - w->offset_y + idx / 2 - idx + offs,
          ur_x - ll_x + idx - offs * 2, ur_y - ll_y + idx - offs * 2, angle1,
          angle2 - 45, ctx->shift_drawtype(draw_type, 2, 0), 0, ish - 1,
          highlight);
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - offs,
          ll_y - w->offset_y + idx / 2 - offs,
          MAX(0, ur_x - ll_x - idx + offs * 2),
          MAX(0, ur_y - ll_y - idx + offs * 2), angle1 + 45, angle2 - 45,
          ctx->shift_drawtype(draw_type, 2, 0), 0, ish - 1, highlight);
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - offs,
          ll_y - w->offset_y + idx / 2 - offs,
          MAX(0, ur_x - ll_x - idx + offs * 2),
          MAX(0, ur_y - ll_y - idx + offs * 2), angle1, angle2 - 45,
          ctx->shift_drawtype(draw_type, -2, 0), 0, ish - 1, highlight);
    } else if (angle1 == 90) {
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - idx + offs,
          ll_y - w->offset_y + idx / 2 - idx + offs,
          ur_x - ll_x + idx - offs * 2, ur_y - ll_y + idx - offs * 2, angle1,
          angle2, ctx->shift_drawtype(draw_type, -2, 0), 0, ish - 1, highlight);
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - offs,
          ll_y - w->offset_y + idx / 2 - offs,
          MAX(0, ur_x - ll_x - idx + offs * 2),
          MAX(0, ur_y - ll_y - idx + offs * 2), angle1, angle2,
          ctx->shift_drawtype(draw_type, 2, 0), 0, ish - 1, highlight);
    } else if (angle1 == 180) {
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - idx + offs,
          ll_y - w->offset_y + idx / 2 - idx + offs,
          ur_x - ll_x + idx - offs * 2, ur_y - ll_y + idx - offs * 2,
          angle1 + 45, angle2 - 45,
          ctx->shift_drawtype(draw_type, 2, 0), 0, ish - 1, highlight);
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - idx + offs,
          ll_y - w->offset_y + idx / 2 - idx + offs,
          ur_x - ll_x + idx - offs * 2, ur_y - ll_y + idx - offs * 2, angle1,
          angle2 - 45, ctx->shift_drawtype(draw_type, -2, 0), 0, ish - 1,
          highlight);
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - offs,
          ll_y - w->offset_y + idx / 2 - offs,
          MAX(0, ur_x - ll_x - idx + offs * 2),
          MAX(0, ur_y - ll_y - idx + offs * 2), angle1 + 45, angle2 - 45,
          ctx->shift_drawtype(draw_type, -2, 0), 0, ish - 1, highlight);
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - offs,
          ll_y - w->offset_y + idx / 2 - offs,
          MAX(0, ur_x - ll_x - idx + offs * 2),
          MAX(0, ur_y - ll_y - idx + offs * 2), angle1, angle2 - 45,
          ctx->shift_drawtype(draw_type, 2, 0), 0, ish - 1, highlight);
    } else if (angle1 == 270) {
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - idx + offs,
          ll_y - w->offset_y + idx / 2 - idx + offs,
          ur_x - ll_x + idx - offs * 2, ur_y - ll_y + idx - offs * 2, angle1,
          angle2, ctx->shift_drawtype(draw_type, 2, 0), 0, ish - 1, highlight);
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - offs,
          ll_y - w->offset_y + idx / 2 - offs,
          MAX(0, ur_x - ll_x - idx + offs * 2),
          MAX(0, ur_y - ll_y - idx + offs * 2), angle1, angle2,
          ctx->shift_drawtype(draw_type, -2, 0), 0, ish - 1, highlight);
    }
  }
  if (border) {
    ctx->gdraw->arc(ll_x - w->offset_x + idx / 2 - idx,
        ll_y - w->offset_y + idx / 2 - idx, ur_x - ll_x + idx,
        ur_y - ll_y + idx, angle1, angle2, glow_eDrawType_Line, 0, 0, highlight);
    if (idx > 0)
      ctx->gdraw->arc(ll_x - w->offset_x + idx / 2,
          ll_y - w->offset_y + idx / 2, MAX(0, ur_x - ll_x - idx),
          MAX(0, ur_y - ll_y - idx), angle1, angle2, glow_eDrawType_Line, 0, 0,
          highlight);
  }
}

void GlowArc::erase(DrawWind *w, void* pos, int hot, void* node)
{
  int ll_x, ll_y, ur_x, ur_y;
  if (w == ctx->navw) {
    hot = 0;
    ll_x = ll.nav_z_x;
    ll_y = ll.nav_z_y;
    ur_x = ur.nav_z_x;
    ur_y = ur.nav_z_y;
  } else {
    ll_x = ll.z_x;
    ll_y = ll.z_y;
    ur_x = ur.z_x;
    ur_y = ur.z_y;
  }
  int idx = int(w->zoom_factor_y / w->base_zoom_factor * line_width - 1);
  idx += hot;
  idx = MAX(0, idx);
  idx = MIN(idx, DRAW_TYPE_SIZE - 1);
  GlowPoint* p = (GlowPoint*)pos;
  if (!fill) {
    ctx->gdraw->arc(ll_x + p->z_x - w->offset_x, ll_y + p->z_y - w->offset_y,
        ur_x - ll_x, ur_y - ll_y, angle1, angle2, glow_eDrawType_LineErase, 0,
        idx);
    // Erase border
    ctx->gdraw->arc(ll_x + p->z_x - w->offset_x + idx / 2 - idx,
        ll_y + p->z_x - w->offset_y + idx / 2 - idx, ur_x - ll_x + idx,
        ur_y - ll_y + idx, angle1, angle2, glow_eDrawType_LineErase, 0, 0);
    ctx->gdraw->arc(ll_x + p->z_x - w->offset_x + idx / 2,
        ll_y + p->z_x - w->offset_y + idx / 2, MAX(0, ur_x - ll_x - idx),
        MAX(0, ur_y - ll_y - idx), angle1, angle2, glow_eDrawType_LineErase, 0, 0);
  } else
    ctx->gdraw->arc(ll_x + p->z_x - w->offset_x, ll_y + p->z_y - w->offset_y,
        ur_x - ll_x, ur_y - ll_y, angle1, angle2, glow_eDrawType_LineErase, 1, 0);
}

int GlowArc::event_handler(void* pos, glow_eEvent event, int x, int y, void* node)
{
  GlowPoint* p = (GlowPoint*)pos;
  if (angle2 == 360 && ll.z_x + p->z_x - ctx->mw->offset_x <= x
      && x <= ur.z_x + p->z_x - ctx->mw->offset_x
      && ll.z_y + p->z_y - ctx->mw->offset_y <= y
      && y <= ur.z_y + p->z_y - ctx->mw->offset_y) {
    return 1;
  } else
    return 0;
}

void GlowArc::get_borders(double pos_x, double pos_y, double* x_right,
    double* x_left, double* y_high, double* y_low, void* node)
{
  if (pos_x + ll.x < *x_left)
    *x_left = pos_x + ll.x;
  if (pos_x + ur.x > *x_right)
    *x_right = pos_x + ur.x;
  if (pos_y + ll.y < *y_low)
    *y_low = pos_y + ll.y;
  if (pos_y + ur.y > *y_high)
    *y_high = pos_y + ur.y;
}

void GlowArc::move(void* pos, double x1, double y1, double x2, double y2,
    int ang1, int ang2, int highlight, int hot)
{
  ll.x = x1;
  ll.y = y1;
  ur.x = x2;
  ur.y = y2;
  angle1 = ang1;
  angle2 = ang2;
  zoom();
  nav_zoom();
  ctx->set_dirty();
}

void GlowArc::move_noerase(void* pos, double x1, double y1, double x2,
    double y2, int ang1, int ang2, int highlight, int hot)
{
  ll.x = x1;
  ll.y = y1;
  ur.x = x2;
  ur.y = y2;
  angle1 = ang1;
  angle2 = ang2;
  zoom();
  nav_zoom();
  ctx->set_dirty();
}

void GlowArc::shift(
    void* pos, double delta_x, double delta_y, int highlight, int hot)
{
  ll.x += delta_x;
  ll.y += delta_y;
  ur.x += delta_x;
  ur.y += delta_y;
  zoom();
  nav_zoom();
  ctx->set_dirty();
}

void GlowArc::export_javabean(GlowTransform* t, void* node,
    glow_eExportPass pass, int* shape_cnt, int node_cnt, int in_nc,
    std::ofstream& fp)
{
  int idx
      = int(ctx->mw->zoom_factor_y / ctx->mw->base_zoom_factor * line_width - 1);
  idx = MAX(0, idx);
  idx = MIN(idx, DRAW_TYPE_SIZE - 1);
  ctx->export_jbean->arc(ll.z_x - ctx->mw->offset_x, ll.z_y - ctx->mw->offset_y,
      ur.z_x - ll.z_x, ur.z_y - ll.z_y, angle1, angle2, fill, !fill, draw_type,
      draw_type, idx, 0.0, 0, glow_eDrawType_No, 0, glow_eGradient_No, 0, 0,
      pass, shape_cnt, node_cnt, fp);
}

