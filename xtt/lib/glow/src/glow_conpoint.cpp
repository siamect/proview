void GlowConPoint::conpoint_select(
    void* pos, int x, int y, double* distance, void** cp)
{
  int px = ((GlowPoint*)pos)->z_x - ctx->mw->offset_x + p.z_x;
  int py = ((GlowPoint*)pos)->z_y - ctx->mw->offset_y + p.z_y;

  double dist = sqrt(1.0 * (x - px) * (x - px) + 1.0 * (y - py) * (y - py));
  if (dist < *distance) {
    *distance = dist;
    *cp = (void*)this;
  }
}

void GlowConPoint::conpoint_select(GlowTransform* t, int x, int y,
    double* distance, void** cp, int* pix_x, int* pix_y)
{
  int px = int(trf.x(t, p.x, p.y) * ctx->mw->zoom_factor_x - ctx->mw->offset_x);
  int py = int(trf.y(t, p.x, p.y) * ctx->mw->zoom_factor_y - ctx->mw->offset_y);

  double dist = sqrt(1.0 * (x - px) * (x - px) + 1.0 * (y - py) * (y - py));
  if (dist < *distance) {
    *distance = dist;
    *cp = (void*)this;
    *pix_x = px;
    *pix_y = py;
  }
}

int GlowConPoint::get_conpoint(
    int num, double* x, double* y, glow_eDirection* dir)
{
  if (number == num) {
    *x = p.x;
    *y = p.y;
    *dir = direction;
    return 1;
  }
  return 0;
}

int GlowConPoint::get_conpoint(GlowTransform* t, int num, bool flip_horizontal,
    bool flip_vertical, double* x, double* y, glow_eDirection* dir)
{
  double rotation;

  if (number == num) {
    *x = trf.x(t, p.x, p.y);
    *y = trf.y(t, p.x, p.y);
    if (direction == glow_eDirection_Center) {
      *dir = direction;
      return 1;
    }
    rotation = (trf.rot(t) / 360 - floor(trf.rot(t) / 360)) * 360;
    if (45 >= rotation || rotation > 315) {
      *dir = direction;
    } else if (45 < rotation && rotation <= 135) {
      switch (direction) {
      case glow_eDirection_Right:
        *dir = glow_eDirection_Up;
        break;
      case glow_eDirection_Up:
        *dir = glow_eDirection_Left;
        break;
      case glow_eDirection_Left:
        *dir = glow_eDirection_Down;
        break;
      case glow_eDirection_Down:
        *dir = glow_eDirection_Right;
        break;
      default:;
      }
    } else if (135 < rotation && rotation <= 225) {
      switch (direction) {
      case glow_eDirection_Right:
        *dir = glow_eDirection_Left;
        break;
      case glow_eDirection_Up:
        *dir = glow_eDirection_Down;
        break;
      case glow_eDirection_Left:
        *dir = glow_eDirection_Right;
        break;
      case glow_eDirection_Down:
        *dir = glow_eDirection_Up;
        break;
      default:;
      }
    } else // if ( 225 < rotation && rotation <= 315)
    {
      switch (direction) {
      case glow_eDirection_Right:
        *dir = glow_eDirection_Down;
        break;
      case glow_eDirection_Up:
        *dir = glow_eDirection_Right;
        break;
      case glow_eDirection_Left:
        *dir = glow_eDirection_Up;
        break;
      case glow_eDirection_Down:
        *dir = glow_eDirection_Left;
        break;
      default:;
      }
    }

    if (flip_horizontal) {
      switch (*dir) {
      case glow_eDirection_Up:
        *dir = glow_eDirection_Down;
        break;
      case glow_eDirection_Down:
        *dir = glow_eDirection_Up;
        break;
      default:;
      }
    }
    if (flip_vertical) {
      switch (*dir) {
      case glow_eDirection_Right:
        *dir = glow_eDirection_Left;
        break;
      case glow_eDirection_Left:
        *dir = glow_eDirection_Right;
        break;
      default:;
      }
    }

    return 1;
  }
  return 0;
}

