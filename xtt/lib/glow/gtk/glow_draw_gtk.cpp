void GlowDrawGtk::gradient_fill_rect(int x, int y, int width, int h,
    glow_eDrawType d0, glow_eDrawType d1, glow_eDrawType d2,
    glow_eGradient gradient)
{
  cairo_t* cr = gdk_cairo_create(w->buffer);

  set_cairo_clip(cr);

  cairo_pattern_t* pat;
  if (!gradient_create_pattern(x, y, width, h, d0, d1, d2, gradient, &pat))
    return;

  cairo_rectangle(cr, x, y, width, h);
  cairo_set_source(cr, pat);
  cairo_fill(cr);

  reset_cairo_clip(cr);
  cairo_pattern_destroy(pat);
  cairo_destroy(cr);
}

void GlowDrawGtk::gradient_fill_rectrounded(int x, int y, int width,
    int h, int roundamount, glow_eDrawType d0, glow_eDrawType d1,
    glow_eDrawType d2, glow_eGradient gradient)
{
  cairo_t* cr = gdk_cairo_create(w->buffer);

  set_cairo_clip(cr);

  cairo_pattern_t* pat;
  if (!gradient_create_pattern(x, y, width, h, d0, d1, d2, gradient, &pat))
    return;

  if (roundamount >= 0) {
    cairo_move_to(cr, x + roundamount, y);
    cairo_line_to(cr, x + width - roundamount, y);
    cairo_arc(cr, x + width - roundamount, y + roundamount, roundamount,
        -M_PI / 2, 0);
    cairo_line_to(cr, x + width, y + h - roundamount);
    cairo_arc(cr, x + width - roundamount, y + h - roundamount, roundamount, 0,
        M_PI / 2);
    cairo_line_to(cr, x + roundamount, y + h);
    cairo_arc(cr, x + roundamount, y + h - roundamount, roundamount, M_PI / 2,
        M_PI);
    cairo_line_to(cr, x, y + roundamount);
    cairo_arc(cr, x + roundamount, y + roundamount, roundamount, M_PI,
        M_PI * 3 / 2);
  } else
    cairo_rectangle(cr, x, y, width, h);

  cairo_set_source(cr, pat);
  cairo_fill(cr);

  reset_cairo_clip(cr);
  cairo_pattern_destroy(pat);
  cairo_destroy(cr);
}

void GlowDrawGtk::gradient_fill_arc(int x, int y, int width, int h,
    int angle1, int angle2, glow_eDrawType d0, glow_eDrawType d1,
    glow_eDrawType d2, glow_eGradient gradient)
{
  if (angle1 >= 360)
    angle1 = angle1 - angle1 / 360 * 360;
  else if (angle1 < 0)
    angle1 = angle1 + (-angle1 / 360 + 1) * 360;

  cairo_t* cr = gdk_cairo_create(w->buffer);

  set_cairo_clip(cr);

  cairo_pattern_t* pat;
  if (!gradient_create_pattern(x, y, width, h, d0, d1, d2, gradient, &pat))
    return;

  cairo_save(cr);
  cairo_translate(cr, double(x) + double(width) / 2, double(y) + double(h) / 2);
  cairo_scale(cr, double(width) / 2, double(h) / 2);
  if (!(angle2 == 360 || angle2 == 180))
    cairo_move_to(cr, 0, 0);
  cairo_arc(cr, 0, 0, 1, -double(angle1 + angle2) / 180 * M_PI,
      -double(angle1) / 180 * M_PI);
  if (!(angle2 == 360 || angle2 == 180))
    cairo_move_to(cr, 0, 0);
  cairo_restore(cr);
  cairo_set_source(cr, pat);
  cairo_fill(cr);

  reset_cairo_clip(cr);
  cairo_pattern_destroy(pat);
  cairo_destroy(cr);
}

void GlowDrawGtk::gradient_fill_polyline(glow_sPointX* points,
    int point_cnt, glow_eDrawType d0, glow_eDrawType d1, glow_eDrawType d2,
    glow_eGradient gradient)
{
  cairo_t* cr = gdk_cairo_create(w->buffer);

  set_cairo_clip(cr);

  double x0, y0, x1, y1;
  x0 = y0 = 1e37;
  x1 = y1 = 1e-37;
  for (int j = 0; j < point_cnt; j++) {
    if (points[j].x < x0)
      x0 = points[j].x;
    if (points[j].y < y0)
      y0 = points[j].y;
    if (points[j].x > x1)
      x1 = points[j].x;
    if (points[j].y > y1)
      y1 = points[j].y;

    if (j == 0)
      cairo_move_to(cr, points[j].x, points[j].y);
    else
      cairo_line_to(cr, points[j].x, points[j].y);
  }

  cairo_pattern_t* pat;
  if (!gradient_create_pattern((int)x0, (int)y0, (int)(x1 - x0),
          (int)(y1 - y0), d0, d1, d2, gradient, &pat))
    return;
  cairo_set_source(cr, pat);
  cairo_fill(cr);

  reset_cairo_clip(cr);
  cairo_pattern_destroy(pat);
  cairo_destroy(cr);
}

void GlowDrawGtk::log_event(GdkEvent* event)
{
  if (ctx->eventlog_callback) {
    GdkEvent e;

    memcpy(&e, event, sizeof(e));

    switch (e.type) {
    case GDK_KEY_PRESS:
      *(gchar*)&e.key.string = e.key.string[0];
      break;
    case GDK_MOTION_NOTIFY: {
      int x, y;

      if (e.motion.is_hint) {
        gdk_window_get_pointer(e.any.window, &x, &y, NULL);
        e.button.x = x;
        e.button.y = y;
        e.motion.is_hint = 0;
      }
      break;
    }
    case GDK_EXPOSE: {
      int width, height;

      get_window_size(ctx->mw, &width, &height);
      if (e.expose.area.x == 0 && e.expose.area.y == 0) {
        e.expose.area.x = -999;
        e.expose.area.y = -999;
        e.expose.area.width = width;
        e.expose.area.height = height;
      }
      break;
    }
    default:;
    }

    (ctx->eventlog_callback)(ctx, &e, sizeof(e));
  }
}

void GlowDrawGtk::event_exec(void* event, unsigned int size)
{
  GdkEvent e;

  if (size != sizeof(e))
    return;

  memcpy(&e, event, size);
  e.any.window = m_wind.window;

  switch (e.type) {
  case GDK_KEY_PRESS: {
    gchar* p = (gchar*)malloc(1);

    *p = *(gchar*)&e.key.string;
    e.key.string = p;
    break;
  }
  case GDK_EXPOSE: {
    if (e.expose.area.x == -999 && e.expose.area.y == -999) {
      set_window_size(ctx->mw, e.expose.area.width, e.expose.area.height);
      e.expose.area.x = 0;
      e.expose.area.y = 0;
    }
    break;
  }
  default:;
  }

  event_handler(e);

  switch (e.type) {
  case GDK_KEY_PRESS: {
    free(e.key.string);
    break;
  }
  default:;
  }
}

int GlowDrawGtk::open_color_selection(double* r, double* g, double* b)
{
  int sts;
  GdkColor color;
  GtkWidget* w = gtk_color_selection_dialog_new("Color selection");
  GtkWidget* csel = gtk_color_selection_dialog_get_color_selection(
      GTK_COLOR_SELECTION_DIALOG(w));
  color.red = int(*r * 0xffff);
  color.green = int(*g * 0xffff);
  color.blue = int(*b * 0xffff);
  gtk_color_selection_set_current_color(GTK_COLOR_SELECTION(csel), &color);
  gint response = gtk_dialog_run(GTK_DIALOG(w));
  if (response == GTK_RESPONSE_OK) {
    gtk_color_selection_get_current_color(GTK_COLOR_SELECTION(csel), &color);
    *r = (double)color.red / 0xffff;
    *g = (double)color.green / 0xffff;
    *b = (double)color.blue / 0xffff;
    sts = 1;
  } else
    sts = 0;
  gtk_widget_destroy(w);
  return sts;
}

void GlowDrawGtk::update_color(glow_eDrawType color)
{
  if (!GlowColor::is_custom(color)) {
    for (int j = 0; j < DRAW_TYPE_SIZE; j++) {
      if (gcs[color][j]) {
        gdk_gc_unref(gcs[color][j]);
        gcs[color][j] = 0;
      }
    }
  } else if (get_customcolors()) {
    // A customcolor
    for (int i = 0; i < 4; i++) {
      for (int j = 0; j < DRAW_TYPE_SIZE; j++) {
        if (get_customcolors()->gcs[color - glow_eDrawType_CustomColor1 + i][j]) {
          gdk_gc_unref(get_customcolors()
                           ->gcs[color - glow_eDrawType_CustomColor1 + i][j]);
          get_customcolors()->gcs[color - glow_eDrawType_CustomColor1 + i][j] = 0;
        }
      }
    }
  }
}

void GlowDrawGtk::push_customcolors(GlowCustomColors* cc)
{
  if (customcolors_cnt > CUSTOMCOLORS_STACK_SIZE) {
    printf("** Max number custom colors exceede\n");
    return;
  }

  for (int i = customcolors_cnt; i > 0; i--)
    customcolors[i] = customcolors[i - 1];
  customcolors[0] = (GlowCustomColorsGtk*)cc;
  customcolors_cnt++;
}

void GlowDrawGtk::set_customcolors(GlowCustomColors* cc)
{
  customcolors[0] = (GlowCustomColorsGtk*)cc;
  customcolors_cnt = 1;
}

void GlowDrawGtk::pop_customcolors()
{
  if (customcolors_cnt <= 0) {
    printf("** Customcolor stack disorder\n");
  }
  for (int i = 0; i < customcolors_cnt - 1; i++)
    customcolors[i] = customcolors[i + 1];
  customcolors_cnt--;
}

GlowCustomColorsGtk* GlowDrawGtk::get_customcolors()
{
  if (customcolors_cnt == 0)
    return NULL;
  return customcolors[0];
}

GlowCustomColors* GlowDrawGtk::create_customcolors()
{
  return new GlowCustomColorsGtk();
}

void GlowDrawGtk::reset_customcolors(GlowCustomColors* cc)
{
  for (int i = 0; i < cc->colors_size; i++) {
    for (int j = 0; j < DRAW_TYPE_SIZE; j++) {
      if (((GlowCustomColorsGtk*)cc)->gcs[i][j])
        gdk_gc_unref(((GlowCustomColorsGtk*)cc)->gcs[i][j]);
    }
  }
  memset(((GlowCustomColorsGtk*)cc)->gcs, 0,
      sizeof(((GlowCustomColorsGtk*)cc)->gcs));

  for (int i = 0;
       i < glow_eDrawType_CustomColor__ - glow_eDrawType_CustomColor1; i++) {
    if (((GlowCustomColorsGtk*)cc)->color_vect[i].pixel != 0)
      gdk_colormap_free_colors(
          colormap, &((GlowCustomColorsGtk*)cc)->color_vect[i], 1);
  }
  memset(((GlowCustomColorsGtk*)cc)->color_vect, 0,
      sizeof(((GlowCustomColorsGtk*)cc)->color_vect));
}
