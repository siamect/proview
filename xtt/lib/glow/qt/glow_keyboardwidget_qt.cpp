#include <stdlib.h>
#include "glow_std.h"

#include "glow_keyboardwidget_qt.h"
#include "glow.h"
#include "glow_ctx.h"
#include "glow_keyboardctx.h"
#include "glow_draw.h"
#include "glow_draw_qt.h"

#include "glow_scroll_widget_qt.h"

static int keyboard_init_proc(QWidget *w, GlowCtx *fctx, void *client_data)
{
  QtScrollWidgetGlow *key_widget = ((QtScrollWidgetGlow *) w);
  KeyboardCtx *ctx = (KeyboardCtx *) key_widget->parent_ctx;

  ctx->configure();

  if (key_widget->init_proc) {
    return (key_widget->init_proc)(ctx, client_data);
  } else {
    return 1;
  }
}

QWidget *keyboardwidgetqt_new(int (*init_proc)(GlowCtx *ctx, void *client_data),
                              void *client_data)
{
  QtScrollWidgetGlow *w = new QtScrollWidgetGlow();
  w->init(glow_eCtxType_Keyboard, init_proc, client_data, keyboard_init_proc);
  return (QWidget *) w;
}

QWidget *keyboardnavwidgetqt_new(QWidget *main_keyboard)
{
  QtScrollWidgetGlow *w = new QtScrollWidgetGlow();
  w->init(glow_eCtxType_Keyboard, main_keyboard);
  return (QWidget *) w;
}