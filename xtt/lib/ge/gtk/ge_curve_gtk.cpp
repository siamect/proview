gboolean GeCurveGtk::action_inputfocus( GtkWidget *w, GdkEvent *event, gpointer data)
{
  GeCurveGtk *curve = (GeCurveGtk *)data;

  if ( curve->focustimer.disabled())
    return TRUE;
  
  curve->set_inputfocus();

  curve->focustimer.disable( 400);
  return FALSE;
}

