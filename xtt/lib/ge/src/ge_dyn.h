public:
  GeDynElem*
      elements; //!< List of dynamic object specified in dyntype and actiontype.
  Graph* graph; //!< Graph.
  bool ignore_color; //!< All remaining elements with lower prio should ignore
  //! color dynamics.
  bool reset_color; //!< An element of higher prio is passing color dynamics to
  //! elements of lower prio.
  bool ignore_bgcolor; //!< All remaining elements with lower prio should ignore
  //! bg color dynamics.
  bool reset_bgcolor; //!< An element of higher prio is passing bg color
  //! dynamics to elements of lower prio.
  bool ignore_invisible; //!< All remaining elements with lower prio should
  //! ignore invisible dynamics.
  bool reset_invisible; //!< An element of higher prio is passing invisible
  //! dynamics to elements of lower prio.
  bool ignore_text_a1; //!< All remaining elements with lower prio should ignore
  //! a1 text dynamics.
