//! This class handles the dynamics for an object.
/*!
  Values for access, cycle, dyntype and actiontype are stored in the class.
  For every bit in dyntype and actiontype, an object for the corresponding
  dynamic class
  is created and inserted into the element list. At dynamic scan and when action
  is executed,
  this list is traversed and scan and action functions for all the elements are
  called.
*/
