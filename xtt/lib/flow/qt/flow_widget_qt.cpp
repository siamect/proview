static void scroll_callback(flow_sScroll *data)
{
  widget_sScroll *scroll_data = (widget_sScroll *) data->scroll_data;
  QtScrollWidgetFlow *flow = ((QtScrollWidgetFlow *) scroll_data->parent);

  if (flow->scroll_timerid) {
    delete flow->scroll_timerid;
  }

  flow->scroll_timerid = new QTimer();
  flow->scroll_timerid->setSingleShot(true);
  QObject::connect(flow->scroll_timerid, SIGNAL(timeout()), flow,
                   SLOT(scroll_callback_cb()));
  flow->scroll_timerid->start(200);
  flow->scroll_data = *data;
}

static int flow_init_proc(QWidget *w, FlowCtx *fctx, void *client_data)
{
  QtScrollWidgetFlow *flow = ((QtScrollWidgetFlow *) w);
  FlowCtx *ctx = (FlowCtx *) flow->parent_ctx;

  if (flow->scroll_h) {
    widget_sScroll *scroll_data = new widget_sScroll();
    scroll_data->parent = w;
    scroll_data->scroll_h = flow->scroll_h;
    scroll_data->scroll_v = flow->scroll_v;
    scroll_data->scroll_h_managed = 1;
    scroll_data->scroll_v_managed = 1;

    ctx->register_scroll_callback((void *) scroll_data, scroll_callback);
  }
  return (flow->init_proc)(ctx, client_data);
}

QWidget *flowwidgetqt_new(int (*init_proc)(FlowCtx *ctx, void *client_data),
                          void *client_data)
{
  QtScrollWidgetFlow *w = new QtScrollWidgetFlow();
  w->init(flow_eCtxType_Flow, init_proc, client_data, flow_init_proc);
  return w;
}

QWidget *scrolledflowwidgetqt_new(
    int (*init_proc)(FlowCtx *ctx, void *client_data), void *client_data,
    QWidget **flowwidget)
{
  QtScrollWidgetFlow *w = new QtScrollWidgetFlow();
  *flowwidget = w;

  return w
      ->initScroll(flow_eCtxType_Flow, init_proc, client_data, flow_init_proc);
}

QWidget *flownavwidgetqt_new(QWidget *main_flow)
{
  QtScrollWidgetFlow *w = new QtScrollWidgetFlow();
  w->init(flow_eCtxType_Flow, main_flow);
  return w;
}

void flowwidgetqt_modify_ctx(QWidget *w, void *ctx)
{
  ((QtScrollWidgetFlow *) w)->parent_ctx = ctx;
}