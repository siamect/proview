gboolean XColWindGtk::action_inputfocus(
    GtkWidget* w, GdkEvent* event, gpointer data)
{
  XColWindGtk* xcolwind = (XColWindGtk*)data;
  gboolean scrolledinput_visible;
  gboolean input_visible;

  if (xcolwind->focustimer.disabled())
    return TRUE;

  g_object_get(
      xcolwind->cmd_scrolledinput, "visible", &scrolledinput_visible, NULL);
  g_object_get(xcolwind->cmd_input, "visible", &input_visible, NULL);
  if (scrolledinput_visible)
    gtk_widget_grab_focus(xcolwind->cmd_scrolledtextview);
  else if (input_visible)
    gtk_widget_grab_focus(xcolwind->cmd_input);
  else if (xcolwind->xattnav)
    xcolwind->xattnav->set_inputfocus();

  xcolwind->focustimer.disable(400);

  return FALSE;
}

void XColWindGtk::change_value_close()
{
  int sts;

  if (input_open) {
    if (input_multiline) {
      gchar *text, *textutf8;
      GtkTextIter start_iter, end_iter;
      gtk_text_buffer_get_start_iter(cmd_scrolled_buffer, &start_iter);
      gtk_text_buffer_get_end_iter(cmd_scrolled_buffer, &end_iter);

      textutf8 = gtk_text_buffer_get_text(
          cmd_scrolled_buffer, &start_iter, &end_iter, FALSE);
      text = g_convert(textutf8, -1, "ISO8859-1", "UTF-8", NULL, NULL, NULL);
      g_free(textutf8);

      if (!text) {
        g_object_set(cmd_scrolledinput, "visible", FALSE, NULL);
        set_prompt("");
        input_open = 0;

        int w, h;
        gdk_drawable_get_size(pane->window, &w, &h);
        gtk_paned_set_position(GTK_PANED(pane), h - 50);

        xattnav->redraw();
        xattnav->set_inputfocus();
        message('E', "Input error, invalid character");
        return;
      }

      sts = xattnav->set_attr_value(input_node, input_name, text);
      g_free(text);
      g_object_set(cmd_scrolledinput, "visible", FALSE, NULL);
      set_prompt("");
      input_open = 0;

      int w, h;
      gdk_drawable_get_size(pane->window, &w, &h);
      gtk_paned_set_position(GTK_PANED(pane), h - 50);

      xattnav->redraw();
      xattnav->set_inputfocus();
    } else {
      char *text, *textutf8;

      textutf8 = gtk_editable_get_chars(GTK_EDITABLE(cmd_input), 0, -1);
      text = g_convert(textutf8, -1, "ISO8859-1", "UTF-8", NULL, NULL, NULL);
      g_free(textutf8);

      if (!text) {
        g_free(text);
        g_object_set(cmd_input, "visible", FALSE, NULL);
        set_prompt("");
        input_open = 0;
        if (redraw_cb)
          (redraw_cb)(this);

        xattnav->set_inputfocus();
        message('E', "Input error, invalid character");
        return;
      }
      sts = xattnav->set_attr_value(input_node, input_name, text);
      g_free(text);
      g_object_set(cmd_input, "visible", FALSE, NULL);
      set_prompt("");
      input_open = 0;
      if (redraw_cb)
        (redraw_cb)(this);

      xattnav->set_inputfocus();
    }
  }
}

void XColWindGtk::activate_cmd_input(GtkWidget* w, gpointer data)
{
  char *text, *textutf8;
  XColWindGtk* xcolwind = (XColWindGtk*)data;
  int sts;

  g_object_set(xcolwind->cmd_prompt, "visible", FALSE, NULL);
  g_object_set(xcolwind->cmd_input, "visible", FALSE, NULL);

  xcolwind->xattnav->set_inputfocus();

  textutf8 = gtk_editable_get_chars(GTK_EDITABLE(w), 0, -1);
  text = g_convert(textutf8, -1, "ISO8859-1", "UTF-8", NULL, NULL, NULL);
  g_free(textutf8);

  if (!text) {
    g_object_set(w, "visible", FALSE, NULL);
    xcolwind->set_prompt("");
    xcolwind->input_open = 0;
    if (xcolwind->redraw_cb)
      (xcolwind->redraw_cb)(xcolwind);
    xcolwind->message('E', "Input error, invalid character");
    return;
  }

  if (xcolwind->input_open) {
    sts = xcolwind->xattnav->set_attr_value(
        xcolwind->input_node, xcolwind->input_name, text);
    g_object_set(w, "visible", FALSE, NULL);
    xcolwind->set_prompt("");
    xcolwind->input_open = 0;
    if (xcolwind->redraw_cb)
      (xcolwind->redraw_cb)(xcolwind);
  }
  g_free(text);
}

