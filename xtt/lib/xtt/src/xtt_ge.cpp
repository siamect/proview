void XttGe::event_exec( int type, void *event, unsigned int size) 
{ 
  switch ( type) {
  case xttlog_eCategory_Event:
    graph->event_exec( event, size);
    break;
  case xttlog_eCategory_GeConfirmOk:
    confirm_reply( 1);
    break;
  case xttlog_eCategory_GeConfirmCancel:
    confirm_reply( 0);
    break;
  default: ;
  }
}

