/* xtt_clog.cpp -- Console log in xtt */

#include <stdlib.h>

#include "co_cdh.h"
#include "co_time.h"
#include "pwr_baseclasses.h"
#include "rt_gdh.h"

#include "cow_wow.h"
#include "xtt_clog.h"
#include "rt_xnav_msg.h"

CLog::CLog(void* clog_parent_ctx, const char* clog_name, pwr_tStatus* status)
    : parent_ctx(clog_parent_ctx), clognav(NULL), clog_displayed(0), help_cb(0),
      close_cb(0), filesel_loaded(false)
{
  *status = 1;
}

//
//  Delete clog
//
CLog::~CLog()
{
}

void CLog::activate_next_file()
{
  set_clock_cursor();
  if (!clognav->next_file())
    wow->DisplayError("File error", "No next file found");
  reset_cursor();
}

void CLog::activate_prev_file()
{
  set_clock_cursor();
  if (!clognav->prev_file())
    wow->DisplayError("File error", "No previous file found");
  reset_cursor();
}

void CLog::activate_print()
{
  print();
}

void CLog::activate_help()
{
  if (help_cb)
    (help_cb)(parent_ctx, "opg_nav_systemmsg");
}
