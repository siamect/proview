void XttCameraControlVapix::area_zoom( int x, int y, int width, int height, int window_width,
				       int window_height, int stream_width, int stream_height)
{
  pwr_tCmd cmd;
  int zoom;
  float fzoom;

  x += width/2;
  y += height/2;

  x = ((float)stream_width) / window_width * x;
  y = ((float)stream_height) / window_height * y; 
  if ( width == 0 || height == 0)
    return;

  fzoom = min( (float)window_width/width, (float)window_height/height);
  zoom = 100 * fzoom;
  if ( zoom > 9999)
    zoom = 9999;

  sprintf( cmd, "wget --ignore-length %s %s %s/axis-cgi/com/ptz.cgi?camera=1\\&areazoom=%d,%d,%d",
	   authstr, outstr, url, x, y, zoom);
  int sts = system( cmd);
  if ( sts != 0)
    printf( "** Error from wget: %d\n", sts >> 8);    
}

// Get current pan, tilt and zoom.
int XttCameraControlVapix::get_position( double *pan, double *tilt, double *zoom)
{
  pwr_tCmd cmd;		  
  pwr_tFileName fname = "/tmp/campos.txt";
  FILE *fp;
  char line[100];
  char *s;
  int sts;
  float cpan, ctilt, czoom;
  int pan_found, tilt_found, zoom_found;
  pwr_tTime old_time;
  pwr_tTime time;
  int old_file_found;
  int new_file_found;
      
  old_file_found = 0;
  sts = dcli_file_time( fname, &old_time);
  if ( ODD(sts))
    old_file_found = 1;

  sprintf( cmd, "wget --ignore-length %s %s %s/axis-cgi/com/ptz.cgi?camera=1\\&query=position --output-document=%s",
	   authstr, outstr, url, fname);
  sts = system( cmd);
  if ( sts != 0)
    printf( "** Error from wget: %d\n", sts >> 8);    

  new_file_found = 0;
  sts = dcli_file_time( fname, &time);
  if ( ODD(sts)) {
    if ( !old_file_found)
      new_file_found = 1;
    else {
      if ( time_Acomp( &old_time, &time) != 0)
	new_file_found = 1;
    }
  }
  if ( !new_file_found)
    return 0;
  
  fp = fopen( fname, "r");
  if (!fp)
    return 0;

  pan_found = 0;
  tilt_found = 0;
  zoom_found = 0;
  while( ODD( dcli_read_line( line, sizeof(line), fp))) {
    if ( (s = strstr( line, "pan=")) != 0) {
      sts = sscanf( s+4, "%f", &cpan);
      if ( sts == 1)
	pan_found = 1;
    }
    else if ( (s = strstr( line, "tilt=")) != 0) {
      sts = sscanf( s+5, "%f", &ctilt);
      if ( sts == 1)
	tilt_found = 1;
    }
    else if ( (s = strstr( line, "zoom=")) != 0) {
      sts = sscanf( s+5, "%f", &czoom);
      if ( sts == 1)
	zoom_found = 1;
    }
    if ( pan_found && tilt_found && zoom_found) {
      break;
    }
  }
  fclose(fp);

  sprintf( cmd, "rm -f %s", fname);
  system( cmd);
      
  if ( !(pan_found && tilt_found && zoom_found))
    return 0;
  
  *pan = (cpan + 180) / 360 * 100;
  *tilt = (ctilt + 180) / 360 * 100;
  *zoom = czoom/100;

  return 1;
}



