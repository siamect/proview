// Absolute pan, tilt and zoom.
void XttCameraControlVapix::pan_tilt_zoom_absolute( double pan, double tilt, double zoom)
{
  pwr_tCmd cmd;
  double ipan = pan / 100 * 360 - 180;
  if ( ipan > 180)
    ipan = 180;
  if ( ipan < -180)
    ipan = -180;
		  
  double itilt = tilt / 100 * 360 - 180;
  if ( itilt > 180)
    itilt = 180;
  if ( itilt < -180)
    itilt = -180;
		  
  int izoom = int(zoom * 100);
  if ( izoom < 1)
    zoom = 1;
  if ( izoom > 19999)
    izoom = 19999;

  sprintf( cmd, "wget --ignore-length %s %s %s/axis-cgi/com/ptz.cgi?camera=1\\&pan=%.2f\\&tilt=%.2f\\&zoom=%d",
	   authstr, outstr, url, ipan, itilt, izoom);
  int sts = system( cmd);
  if ( sts != 0)
    printf( "** Error from wget: %d\n", sts >> 8);    
}

// Center coordinates. Coordinates in current camera window size. 
