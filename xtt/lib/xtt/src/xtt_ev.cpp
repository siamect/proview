int	Ev::outunit_connect( pwr_tObjid	user)
{
  int		sts;

  // Wait for mh has flagged initizated
  mh_UtilWaitForMh();

  sts = mh_OutunitConnect(
		user,
		mh_eOutunitType_Operator,
		0,
		mh_ack_bc,
		mh_alarm_bc,
		mh_block_bc,
		mh_cancel_bc,
		mh_clear_alarmlist_bc,
		mh_clear_blocklist_bc,
		mh_info_bc,
		mh_return_bc
		);
  if (EVEN(sts)) return sts;

  connected = 1;
  return XNAV__SUCCESS;
}

void Ev::update( double scantime)
{
  int sts;
  int nodraw_set = 0;

  sts = mh_OutunitReceive();     
  while (ODD(sts))
  {
    if ( !nodraw_set)
    {
      eve->set_nodraw();
      ala->set_nodraw();
      nodraw_set = 1;
    }
    sts = mh_OutunitReceive();     
  }
  if ( nodraw_set)
  {
    eve->reset_nodraw();
    ala->reset_nodraw();
  }

  ala->flash();
  for ( int i = 0; i < sala_cnt; i++)
    sala[i]->update();
  if ( beep)
    ala->beep( scantime);
}      

void Ev::ack_last_prio( unsigned long type, unsigned long prio)
{
  mh_sEventId 	*id;
  int		sts;

  if (is_authorized_cb && !is_authorized_cb( parent_ctx, pwr_mAccess_RtEventsAck | pwr_mAccess_System))
    return;

  sts = ala->get_last_not_acked_prio( &id, type, prio);
  if ( ODD(sts))
  {
    mh_sEventId lid = *id;

    ala->ack( id);
    eve->ack( id);
    for ( int i = 0; i < sala_cnt; i++)
      sala[i]->ack( id);
    for ( int i = 0; i < seve_cnt; i++)
      seve[i]->ack( id);
    mh_OutunitAck( &lid);
  }
}

void Ev::ack_all()
{
  mh_sEventId *id;
  int sts;

  if (is_authorized_cb && !is_authorized_cb( parent_ctx, pwr_mAccess_RtEventsAck | pwr_mAccess_System))
    return;

  sts = ala->get_last_not_acked( &id);
  while ( ODD(sts)) {
    mh_sEventId lid = *id;
    ala->ack( id);
    eve->ack( id);
    for ( int i = 0; i < sala_cnt; i++)
      sala[i]->ack( id);
    for ( int i = 0; i < seve_cnt; i++)
      seve[i]->ack( id);
    mh_OutunitAck( &lid);

    sts = ala->get_last_not_acked( &id);
  }
}

