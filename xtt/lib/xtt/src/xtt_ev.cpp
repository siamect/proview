void Ev::sala_close_cb( void *ctx, EvAla *sala)
{
  Ev *ev = (Ev *)ctx;

  bool found = false;
  for ( int i = 0; i < ev->sala_cnt; i++) {
    if ( ev->sala[i] == sala)
      found = true;
    if ( found && i != ev->sala_cnt - 1)
      ev->sala[i] = ev->sala[i+1];
  }
  if ( found)
    ev->sala_cnt--;
}

