int Ev::sala_acknowledge_cb( void *ctx, mh_sEventId *id)
{
  Ev *ev = (Ev *)ctx;

  mh_sEventId lid = *id;
  ev->ala->ack( id);
  ev->eve->ack( id);
  for ( int i = 0; i < ev->sala_cnt; i++)
    ev->sala[i]->ack( id);
  for ( int i = 0; i < ev->seve_cnt; i++)
    ev->seve[i]->ack( id);
  mh_OutunitAck( &lid);
  return 1;
}

