#ifndef xtt_hotkey_qt_h
#define xtt_hotkey_qt_h

#if defined OS_LINUX

#include <vector>

#include <QEvent>

class HotkeyAction {
public:
  char m_name[80];
  void (*m_action)(char*, void*);
  HotkeyAction(const char* name, void (*action)(char*, void*));
  HotkeyAction(const HotkeyAction& x);
};

class HotkeyKey {
public:
  int m_mod;
  int m_key;
  char m_action_name[80];
  char m_action_arg[200];
  void (*m_action)(char*, void*);
  void* m_userdata;

  HotkeyKey(int mod, int key, char* action_name, char* action_arg);
  HotkeyKey(const HotkeyKey& x);
  void set_action(HotkeyAction* action, void* userdata);
};

class XttHotkey {
public:
  pwr_tFileName m_filename;
  std::vector<HotkeyAction> m_actions;
  std::vector<HotkeyKey> m_keys;
  XttHotkey(const char* filename);
  ~XttHotkey() {}
  void register_action(
      const char* name, void (*action)(char*, void*), void* userdata);
  int read_file();
  static int event_handler(QEvent* xevent, QObject* obj);
};

#else
// Dummy for other platforms than OS_LINUX

class XttHotkey {
public:
  XttHotkey(const char* filename) {}

  ~XttHotkey() {}

  void register_action(
      const char* name, void (*action)(char*, void*), void* userdata) {}

  static int event_handler(QEvent* xevent, QObject* obj)
  {
    return 1;
  }
};

#endif

#endif
