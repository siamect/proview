  void ala_action_inputfocus();
  void blk_action_inputfocus();
  void eve_activate_exit();
  void ala_activate_exit();
  void blk_activate_exit();
  void eve_activate_print();
  void ala_activate_print();
  void blk_activate_print();
  void eve_activate_export();
  void eve_activate_analyse();
  void eve_activate_ack_last();
  void ala_activate_ack_last();
  void ala_activate_ack_all();
  void eve_activate_zoom_in();
  void ala_activate_zoom_in();
  void blk_activate_zoom_in();
  void eve_activate_zoom_out();
  void ala_activate_zoom_out();
  void blk_activate_zoom_out();
  void eve_activate_zoom_reset();
  void ala_activate_zoom_reset();
  void blk_activate_zoom_reset();
  void blk_activate_block_remove();
  void eve_activate_open_plc();
  void ala_activate_open_plc();
  void blk_activate_open_plc();
  void eve_activate_display_in_xnav();
  void ala_activate_display_in_xnav();
  void blk_activate_display_in_xnav();
  void eve_activate_disp_hundredth(bool set);
  void ala_activate_disp_hundredth(bool set);
  void eve_activate_hide_object(bool set);
  void ala_activate_hide_object(bool set);
  void eve_activate_hide_text(bool set);
  void ala_activate_hide_text(bool set);
