/* xtt_evlist_qt.cpp -- Alarm or event list in xtt */

#include "co_dcli.h"
#include "co_syi.h"

#include "cow_qt_helpers.h"
#include "cow_wow_qt.h"

#include "flow_browwidget_qt.h"

#include "xtt_evlist_qt.h"

#include <QApplication>

EvListQt::EvListQt(void* ev_parent_ctx, ev_eType ev_type, int ev_size,
    int ev_eventname_seg, QWidget** w, void (*ev_init_cb)(void*))
    : EvList(ev_parent_ctx, ev_type, ev_size, ev_eventname_seg, ev_init_cb)
{
  form_widget = scrolledbrowwidgetqt_new(init_brow_cb, this, &brow_widget);

  form_widget->show();

  *w = form_widget;
}

EvListQt::~EvListQt()
{
  if (browtree) {
    delete browtree->ctx;
  }
  if (browtree && browtree != brow) {
    delete browtree;
  }
  if (browbase && browbase != brow) {
    delete browbase;
  }
  delete brow;

  form_widget->close();
}

void EvListQt::set_input_focus()
{
  brow_widget->setFocus();
}

void EvListQt::bell()
{
  QApplication::beep();
}

void EvListQt::popup_position(int x_event, int y_event, int* x, int* y)
{
  CoWowQt::PopupPosition(brow_widget, x_event, y_event, x, y);
}

void EvListQt::print(const char* title)
{
  char nodename[80];
  pwr_tStatus sts;
  char print_title[120];

  strcpy(print_title, title);
  syi_NodeName(&sts, nodename, sizeof(nodename));
  if (ODD(sts)) {
    strcat(print_title, " ");
    strcat(print_title, nodename);
  }

  sts = CoWowQt::CreateBrowPrintDialogQt(print_title, brow->ctx, brow_widget);
  if (sts == WOW__PRINTDIALOGDISABLED) {
    pwr_tFileName filename;
    pwr_tCmd cmd;

    dcli_translate_filename(filename, "$pwrp_tmp/xnav.ps");
    print_nodia(filename);

    sprintf(cmd, "$pwr_exe/rt_print.sh %s", filename);
    system(cmd);
  }
}
