#ifndef cow_rtmon_h
#define cow_rtmon_h

/* cow_rtmon.h -- Status Monitor node list */

#ifndef cow_statusmon_nodelistnav
#include "cow_statusmon_nodelistnav.h"
#endif

class CoWow;
class MsgWindow;

class RtMon {
public:
  RtMon(void* rtmon_parent_ctx, const char* rtmon_name,
      const char* rtmon_display, pwr_tStatus* status);
  virtual ~RtMon();

  void* parent_ctx;
  char name[80];
  NodelistNav* nodelistnav;
  int rtmon_displayed;
  void (*help_cb)(void*, const char*);
  void (*close_cb)(void*);
  void (*india_ok_cb)(RtMon*, char*);
  CoWow* wow;
  char nodename[40];
  char display[80];

  virtual void pop()
  {
  }
  virtual void set_clock_cursor()
  {
  }
  virtual void reset_cursor()
  {
  }
  virtual void free_cursor()
  {
  }

  void set_scantime(float scantime)
  {
    nodelistnav->scantime = int(scantime * 1000);
  }
  void activate_help();
  void activate_start();
  void activate_restart();
  void activate_stop();
  void activate_reset();
  void activate_xtt();
  void activate_op();
  static void stop_ok_cb(void* ctx, void* data);
  static void reset_ok_cb(void* ctx, void* data);
};

#endif
