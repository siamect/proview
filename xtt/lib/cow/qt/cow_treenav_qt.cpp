/* cow_treenav.cpp -- Tree viewer */

#include "cow_qt_helpers.h"
#include "cow_treenav_qt.h"

#include "flow_browwidget_qt.h"

TreeNavQt::TreeNavQt(void* xn_parent_ctx, QWidget* xn_parent_wid,
    pwr_tAttrRef* xn_itemlist, int xn_item_cnt, unsigned int xn_options,
    pwr_tStatus (*xn_get_object_info)(
        void*, pwr_tAttrRef*, char*, int, char*, char*, int),
    pwr_tStatus (*xn_get_node_info)(void*, char*, char*, int), QWidget** w,
    pwr_tStatus* status)
    : TreeNav(xn_parent_ctx, xn_itemlist, xn_item_cnt, xn_options,
          xn_get_object_info, xn_get_node_info, status)
{
  debug_print("creating a scrolledbrowwidgetqt\n");
  form_widget
      = scrolledbrowwidgetqt_new(TreeNav::init_brow_cb, this, &brow_widget);

  showNow(brow_widget);

  // Create the root item
  *w = form_widget;

  *status = 1;
}

TreeNavQt::~TreeNavQt()
{
  debug_print("TreeNavQt::~TreeNavQt\n");
  delete brow;
  form_widget->close();
}

void TreeNavQt::set_inputfocus()
{
  brow_widget->setFocus();
}