}



wb_adm_parse ()
{

  local cmd

  cmd="opendb"
  if [ -z $1 ] || [ $1 = $cmd ] || [ ${cmd#$1} != $cmd ]; then
    shift
    wb_adm_opendb_func $1 $2 $3 $4 $5
    return
  fi
  cmd="create"
  if [ -z $1 ] || [ $1 = $cmd ] || [ ${cmd#$1} != $cmd ]; then
    shift
    wb_adm_create_func $@
    return
  fi
  echo "Unknown command"
}

#set -o xtrace
wb_adm_parse $@
