/* wb.cpp -- graphical editor */

#include "co_dcli.h"
#include "co_string.h"

int main(int argc, char* argv[])
{
  for (int i = 1; i < argc; i++) {
    if (streq(argv[i], "-f")) {
      if (i + 1 >= argc) {
        // Wb::usage();
        exit(0);
      }
      dcli_execute_flavour_if_exists(argv, argv[i + 1]);
    }
  }

  dcli_execute_flavour(argv);
}
