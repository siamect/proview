void WRevGtk::activate_exit( GtkWidget *w, gpointer data)
{
  WRev *wrev = (WRev *)data;

  if ( wrev->dialog_count != 0)
    return;

  if ( wrev->close_cb)
    (wrev->close_cb)( wrev->parent_ctx);
  else
    delete wrev;
}

