#ifndef wb_bdrep_h
#define wb_bdrep_h

#include "wb_name.h"

class wb_adrep;
class wb_orep;
class wb_merep;

class wb_bdrep {
  int m_nRef;
  wb_orep* m_orep;
  pwr_tStatus m_sts;
  pwr_tObjName m_structname;
  wb_merep* m_merep;

public:
  wb_bdrep();
  wb_bdrep(wb_orep& o); // own orep
  wb_bdrep(wb_adrep* adrep);
  ~wb_bdrep();

  void unref();
  wb_bdrep* ref();

  // pwr_sAttrRef aref() { pwr_sAttrRef a; return a;} // Fix
  pwr_sAttrRef aref();
  size_t size();
  int nAttribute();
  pwr_eBix bix();
  pwr_tOid boid();
  pwr_tCid bcid();

  const char* name() const; // get body  name
  wb_name longName() const;

  wb_adrep* adrep(pwr_tStatus* sts); // Get first attribute
  wb_adrep* adrep(pwr_tStatus* sts, const char* aname);
  wb_adrep* super(pwr_tStatus* sts);

  pwr_tStatus sts()
  {
    return m_sts;
  }
  pwr_tTime modTime();
  char* structName();
  void merep(wb_merep* me)
  {
    m_merep = me;
  }
};

#endif
