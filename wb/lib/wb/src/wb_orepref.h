#ifndef wb_orepref_h
#define wb_orepref_h

#include "wb_orep.h"

class wb_vrepref;

class wb_orepref : public wb_orep {
  wb_vrepref* m_vrep;
  unsigned int m_refCount;
  pwr_tCid m_cid;

  friend class wb_vrepref;

public:
  wb_orepref(wb_vrepref* vrep, int oix);
  ~wb_orepref();

  virtual pwr_tOid oid() const;
  virtual pwr_tVid vid() const;
  virtual pwr_tOix oix() const;

  virtual pwr_tCid cid() const;
  virtual pwr_tOid poid() const;
  virtual pwr_tOid foid() const;
  virtual pwr_tOid loid() const;
  virtual pwr_tOid boid() const;
  virtual pwr_tOid aoid() const;

  virtual const char* name() const;
  virtual wb_name longName();
  virtual pwr_tTime ohTime() const;
  virtual pwr_tTime rbTime() const;
  virtual pwr_tTime dbTime() const;
  virtual pwr_mClassDef flags() const;

  virtual bool isOffspringOf(const wb_orep* o) const;
  wb_orep* ancestor(pwr_tStatus* sts);
  wb_orep* parent(pwr_tStatus* sts);
  wb_orep* after(pwr_tStatus* sts);
  wb_orep* before(pwr_tStatus* sts);
  wb_orep* first(pwr_tStatus* sts);
  wb_orep* child(pwr_tStatus* sts, wb_name& name);
  wb_orep* last(pwr_tStatus* sts);
  wb_orep* next(pwr_tStatus* sts);
  wb_orep* previous(pwr_tStatus* sts);
  virtual wb_adrep* attribute(pwr_tStatus* sts, const char* name);
  virtual wb_adrep* attribute(pwr_tStatus* sts);
  wb_erep* erep() const;
  wb_vrep* vrep() const;
  virtual ldh_eVolRep vtype() const;

  virtual bool docBlock(char** block, int* size) const;
  virtual bool docBlock(char* block);
};

#endif
