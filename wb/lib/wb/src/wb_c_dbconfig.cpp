/* wb_c_plcpgm.c -- work bench methods of the PlcPgm class. */

#include "pwr_baseclasses.h"

#include "co_dcli.h"
#include "co_string.h"

#include "cow_login.h"

#include "wb_pwrs.h"

/*----------------------------------------------------------------------------*\
  Open a workbench from a DbConfig object.
\*----------------------------------------------------------------------------*/

static pwr_tStatus OpenDb(ldh_sMenuCall* ip)
{
  int size;
  char* db_id_p;
  char db_id[80];
  int sts;
  char name[80];
  char filename[80];
  char cmd[256];

  sts = ldh_GetObjectPar(ip->PointedSession, ip->Pointed.Objid, "RtBody", "Id",
      (char**)&db_id_p, &size);
  if (EVEN(sts))
    return sts;

  sts = ldh_ObjidToName(ip->PointedSession, ip->Pointed.Objid, ldh_eName_Object,
      name, sizeof(name), &size);
  if (EVEN(sts))
    return sts;

  dcli_translate_filename(filename, "$pwr_exe/wb_open_db.sh");
  str_ToLower(db_id, db_id_p);
  sprintf(cmd, "%s \"%s\" \"%s\" \"%s\" \"\" \"%s\" &", filename, db_id,
      CoLogin::username(), CoLogin::ucpassword(), name);
  free(db_id_p);

  sts = system(cmd);
  if (sts == -1 || sts == 127) {
    printf("-- Error when creating process.\n");
    return sts;
  }
  return 1;
}

/*----------------------------------------------------------------------------*\
  Every method to be exported to the workbench should be registred here.
\*----------------------------------------------------------------------------*/

pwr_dExport pwr_BindMethods(DbConfig)
    = { pwr_BindMethod(OpenDb), pwr_NullMethod };
