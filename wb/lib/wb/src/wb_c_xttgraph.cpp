/* wb_c_xttgraph.c -- work bench methods of the XttGraph class. */

using namespace std;

#include "co_cdh.h"
#include "wb_wnav.h"
#include "wb_pwrb_msg.h"
#include "wb_build.h"
#include "wb_wtt.h"


/*----------------------------------------------------------------------------*\
  To open Ge.
\*----------------------------------------------------------------------------*/

static pwr_tStatus OpenGraph (
  ldh_sMenuCall *ip
)
{
  int 		sts;
  char		*action;
  int		size;
  char		graph_name[80];

  sts = ldh_GetObjectPar( ip->PointedSession, ip->Pointed.Objid, "RtBody",
			"Action", &action, &size); 
  if ( EVEN(sts)) return sts;

  cdh_ToLower( graph_name, action);
  free( (char *)action);
  if ( strstr( graph_name, ".pwg")) {
    ip->wtt->ge_new( graph_name);
    return 1;
  }
  return PWRB__GRAPHACTION;
}

static pwr_tStatus Build (
  ldh_sMenuCall *ip
)
{
  wb_build build( *(wb_session *)ip->PointedSession, ip->wnav);

  build.opt = ip->wnav->gbl.build;
  build.xttgraph( ip->Pointed.Objid);

  if ( build.sts() == PWRB__NOBUILT)
    ip->wnav->message( 'I', "Nothing to build");
  return build.sts();
}

static pwr_tStatus PostRename (
  ldh_tSesContext   Session,
  pwr_tOid	    Object
) {
  pwr_tStatus sts;
  int size;
  pwr_tString80 name;
  char *action;

  sts = ldh_GetObjectPar( Session, Object, "RtBody",
			"Action", &action, &size); 
  if ( EVEN(sts)) return sts;

  if ( strcmp( action, "") == 0) {
    sts = ldh_ObjidToName( Session, Object, ldh_eName_Object, name, sizeof(name), &size);
    if ( EVEN(sts)) return sts;

    cdh_ToLower( name, name);
    strcat( name, ".pwg");

    sts = ldh_SetObjectPar( Session, Object, "RtBody", "Action", name,
			  sizeof(name));
    if ( EVEN(sts)) return sts;
  }
  free( action);

  return PWRB__SUCCESS;
}


/*----------------------------------------------------------------------------*\
  Every method to be exported to the workbench should be registred here.
\*----------------------------------------------------------------------------*/

pwr_dExport pwr_BindMethods(XttGraph) = {
  pwr_BindMethod(OpenGraph),
  pwr_BindMethod(Build),
  pwr_BindMethod(PostRename),
  pwr_NullMethod
};





