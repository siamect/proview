/*************************************************************************
*
* Name:		dir_parse_filename
*
* Type		int
*
* Description:
*	Parses a filename in device, directory, namn, type and version.
*
* Parameters
*	filename	*char	I  Filenamn that will be parsed.
*	dev		*char	O  device.
*	dir		*char	O  directory.
*	file		*char	O  filenamn.
*	type		*char	O  type.
*	version		*int	O  version.
*
**************************************************************************/

pwr_tStatus dir_parse_filename(
    char* filename, char* dev, char* dir, char* file, char* type, int* version)
{
  char *s, *t;
  char ldev[200];
  char ldir[200];
  char lfile[200];
  char ltype[80];
  char lversion[80];
  int sts;

  if ((s = strstr(filename, "::")))
    s += 2;
  else
    s = filename;
  strcpy(ldev, s);

  /* Device */
  if ((s = strchr(ldev, ':')))
    s++;
  else
    s = ldev;
  strcpy(ldir, s);
  *s = 0;

  /* Directory */
  if ((s = strchr(ldir, '>')))
    s++;
  else {
    if ((s = strchr(ldir, ']')))
      s++;
    else
      s = ldir;
  }
  strcpy(lfile, s);
  *s = 0;

  /* File */
  if ((s = strchr(lfile, '.')) == 0)
    s = lfile;
  strcpy(ltype, s);
  *s = 0;

  /* Type */
  if ((s = strchr(ltype, ';')))
    t = s++;
  else {
    if ((s = strchr(ltype + 1, '.')))
      t = s++;
    else {
      s = ltype;
      t = s + strlen(s);
    }
  }
  strcpy(lversion, t);
  *t = 0;

  if (streq(lversion, "")) {
    *version = 0;
  } else {
    sts = sscanf(lversion, "%d", version);
    if (sts != 1)
      *version = 0;
  }

  strcpy(dev, ldev);
  strcpy(dir, ldir);
  strcpy(file, lfile);
  strcpy(type, ltype);
  return 1;
}

/****************************************************************************
* Name:		dir_get_fileinfo ()
*
* Type		int
*
* Type		Parameter	IOGF	Description
*
* Description:
* 	Send back creation time and size of a given file
*
**************************************************************************/
pwr_tStatus dir_get_fileinfo(char* file_name, pwr_tTime* time2_ptr, int* size_p,
    int* vmsvers_p, char* found_file)
{
  struct stat info;
  int sts;
  char fname[200];

  dcli_translate_filename(fname, file_name);
  sts = stat(fname, &info);
  if (sts == -1)
    return 0;

  if (time2_ptr) {
    time2_ptr->tv_sec = info.st_ctime;
    time2_ptr->tv_nsec = 0;
  }
  if (size_p)
    *size_p = (int)info.st_size;
  if (vmsvers_p)
    *vmsvers_p = 0;
  if (found_file != NULL)
    strcpy(found_file, file_name);

  return 1;
}

/*************************************************************************
*
* Name:		dir_TimeString
*
* Type		*char
*
* Description:
*		Converts a VMS-time to a string.
*
* Parameters
*
**************************************************************************/

char* dir_TimeString(pwr_tTime* time, char* timestr)
{
  static char timstr[32];

  time_AtoAscii(time, time_eFormat_DateAndTime, timstr, sizeof(timstr));
  if (timestr != NULL)
    strcpy(timestr, &timstr[5]);

  return timstr;
}

