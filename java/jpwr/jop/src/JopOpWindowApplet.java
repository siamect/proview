package jpwr.jop;
import java.util.*;
import java.applet.Applet;
import java.awt.*;
import java.awt.event.*;
import java.awt.font.*;
import java.awt.geom.*;
import java.awt.image.*;
import javax.swing.*;
import javax.swing.event.*;
import jpwr.rt.*;

/** 
    Applet for the operator window.
*/
public class JopOpWindowApplet extends JopApplet
{
  static final long serialVersionUID = -5617832787168154292L;
  JScrollPane scrollPane;
  JPanel contentPane;
  BorderLayout borderLayout1 = new BorderLayout();
  JopOpWindow localPanel;
  Dimension size;

  public void setSize(int width, int height)
  {
    Logg.logg("JopXttApplet: setSize()", 1);

    super.setSize(width, height);

    validate();
  }


  public String GetAppletInfo()
  {
    return "Applet information";
  }

  public String[][] getParameterInfo()
  {
    return null;
  }
  public void destroy()
  {
    // session.close();
      super.destroy();
  }

  public void setLabelText( String text) {
    localPanel.setLabelText( text);
  }

  public void setLanguage( int language) {
    localPanel.setLanguage( language);
  }

  public void init()
  {
    super.init();
    setSize(new Dimension(370, 570));
    String pwrPlace = this.getParameter("pwrplace");

    System.out.println( "pwrPlace " + pwrPlace);
    if ( pwrPlace == null) {
	System.out.println( "No pwrPlace specified in applet");
	return;
    }

    if ( pwrPlace.startsWith("$node")) {
	// Replace with node object
	CdhrObjid roid = engine.gdh.getNodeObject(0);
	String name = engine.gdh.objidToName( roid.objid, Cdh.mName_pathStrict).str;
	pwrPlace = name + pwrPlace.substring(5);
    }

    localPanel = new JopOpWindow( session, (Object)this, pwrPlace);
    contentPane = (JPanel) this.getContentPane();
    contentPane.setLayout(borderLayout1);
    scrollPane = new JScrollPane(localPanel);
    contentPane.add(scrollPane, BorderLayout.CENTER);
    contentPane.setOpaque(true);
    localPanel.setLayout(new BoxLayout( localPanel, BoxLayout.Y_AXIS));
    localPanel.setOpaque(true);
    localPanel.setBackground( Color.white);
    // this.setTitle("Operator Window");
    size = new Dimension( 300, 900);
    setSize( size);
    engine.setFrameReady();

  }

  public void start() {
  }


  public void stop() {
  }


}

