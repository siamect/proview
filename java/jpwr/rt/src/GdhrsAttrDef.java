package jpwr.rt;
import java.io.Serializable;
import jpwr.rt.PwrsParInfo;

/**
 *  Return class for attribute definitions.
 *  @author	JN
 */
public class GdhrsAttrDef implements Serializable
{
  static final long serialVersionUID = 1389290116684738472L;
  public String attrName;
  public int attrLevel;
  public int attrClass;
  public int flags;
  public int typeRef;
  public PwrsParInfo info;
  public int sts;
  
  public GdhrsAttrDef( String attrName,
		       int attrLevel,
		       int attrClass,
		       int flags,
		       int typeRef,
		       PwrsParInfo info,
		       int sts) {
    this.attrName = attrName;
    this.attrLevel = attrLevel;
    this.attrClass = attrClass;
    this.flags = flags;
    this.info = info;
    this.typeRef = typeRef;
    this.sts = sts;
  }
  /** Check if status is even. */
  public boolean evenSts() { return (sts % 2 == 0);}
  /** Get the status. */
  public boolean oddSts() { return (sts % 2 == 1);}
  /** Get the type identifier. */
  public int getSts() { return sts;}
}
