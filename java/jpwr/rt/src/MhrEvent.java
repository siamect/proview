package jpwr.rt;
import java.io.Serializable; 

/**
 *  Return class for function returning mh events.
 */
public class MhrEvent implements Serializable
{
  static final long serialVersionUID = -4601859409348816451L;
  public String eventTime;
  public String eventText;
  public String eventName;
  public int eventFlags;
  public int eventStatus;
  public int eventPrio;
  public MhrsEventId eventId;
  public MhrsEventId targetId;
  public int eventType;
  public PwrtAttrRef object;
  public PwrtAttrRef supObject;
  public String eventMoreText;
  public int syncIdx;

  public MhrEvent(String eventText, 
                  String eventName, 
		  String eventTime, 
		  int eventFlags, 
		  int eventPrio, 
		  int eventStatus, 
		  int eventId_nix, 
		  String eventId_birthTime, 
		  int eventId_idx,
		  int targetId_nix, 
		  String targetId_birthTime, 
		  int targetId_idx,
		  int eventType,
		  PwrtAttrRef object,
		  PwrtAttrRef supObject,
		  String eventMoreText)
  {
    this.eventText = eventText;
    this.eventName = eventName;
    this.eventTime = eventTime;
    this.eventFlags = eventFlags;
    this.eventPrio = eventPrio;
    this.eventStatus = eventStatus;
    this.eventId = new MhrsEventId(eventId_nix, eventId_birthTime, eventId_idx); 
    this.targetId = new MhrsEventId(targetId_nix, targetId_birthTime, targetId_idx);
    this.eventType = eventType;
    this.object = object;
    this.supObject = supObject;
    this.eventMoreText = eventMoreText;
  }
  public MhrEvent getCopy()
  {
    MhrEvent ev = new MhrEvent(eventText, 
			       eventName, 
			       eventTime, 
			       eventFlags, 
			       eventPrio, 
			       eventStatus, 
			       eventId.nix, 
			       eventId.birthTime, 
			       eventId.idx,
			       targetId.nix, 
			       targetId.birthTime, 
			       targetId.idx,
			       eventType,
			       object,
			       supObject,
			       eventMoreText);
    ev.syncIdx = syncIdx;
    return ev;
  }
  public String toString()
  {
    return new String(eventTime + eventText + eventName + eventFlags + eventPrio + eventType);
  }

}

