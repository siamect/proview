package jpwr.rt;
import java.io.Serializable;

/**
 * Attribute information.
 * @author	JN
 */
public class PwrsParInfo implements Serializable
{
  static final long serialVersionUID = 6346958989687459363L;
  public String PgmName;
  public int   Type;
  public int    Offset;
  public int    Size;
  public int    Flags;
  public int    Elements;
  public int    ParamIndex;
  public PwrsParInfo( String name, 
                      int type,
		      int offset,
		      int size,
		      int flags,
		      int elements,
		      int paramindex) {
    this.PgmName = name;
    this.Type = type;
    this.Offset = offset;
    this.Size = size;
    this.Flags = flags;
    this.Elements = elements;
    this.ParamIndex = paramindex;
  }
}
