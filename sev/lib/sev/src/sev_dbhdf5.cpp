int sev_dbhdf5::time_to_idx( hid_t dataset_id, hid_t memspace_id, hid_t dataspace_id,
			     hid_t mtype, unsigned int stime, int size, int low_idx, 
			     int high_idx, unsigned int low_time, unsigned int high_time, 
			     int resolution, unsigned int prev_time, int *iter, int *ridx)
{
  unsigned int time;
  int idx;

  (*iter)++;
  if ( *iter > 50)
    return 0;

  if ( high_idx < low_idx)
    high_idx += size;

  idx = (int)((float)(high_idx - low_idx)/ (high_time - low_time) * (stime - low_time) + low_idx + 0.5);
  if ( idx >= size)
    idx -= size;
  if ( idx >= size)
    idx = size - 1;
  get_time( dataset_id, memspace_id, dataspace_id, mtype, idx, &time);
  printf( "%d Search time : %d %d\n", idx, stime, time);
  if ( abs( time - stime) <= resolution) {
    *ridx = idx;
    return 1;
  }
  if ( prev_time && abs( time - prev_time) <= 1) {
    if ( abs( time - low_time) > abs( high_time - time)) 
      idx = high_idx - ( high_idx - low_idx) / 10;
    else
      idx = low_idx + ( high_idx - low_idx) / 10;
    get_time( dataset_id, memspace_id, dataspace_id, mtype, idx, &time);
    printf( "%d Search time : %d %d\n", idx, stime, time);
    if ( abs( time - stime) <= resolution) {
      *ridx = idx;
      return 1;
    }
  }

  if ( time < stime) {
    low_time = time;
    low_idx = idx;
  }
  else {
    high_time = time;
    high_idx = idx;
  }
  return time_to_idx( dataset_id, memspace_id, dataspace_id, mtype, stime, size, low_idx, 
		      high_idx, low_time, high_time, resolution, time, iter, ridx);
}

