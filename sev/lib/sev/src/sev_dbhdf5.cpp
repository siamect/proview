  case pwr_eType_UInt8: dtype = sev_eDataType_UInt8; break;
  case pwr_eType_UInt16: dtype = sev_eDataType_UInt16; break;
  case pwr_eType_UInt32: dtype = sev_eDataType_UInt32; break;
  case pwr_eType_UInt64: dtype = sev_eDataType_UInt64; break;
  case pwr_eType_Float32: dtype = sev_eDataType_Float32; break;
  case pwr_eType_Float64: dtype = sev_eDataType_Float64; break;
