#ifndef pwr_version_h
#define pwr_version_h

/*
  File: pwr_inc:pwr_version.h

  Description:
    Defines version varibles to use in messages to user.
*/

#include "pwr.h"

#if defined OS_LINUX
#define pwrv_cOpSys "Linux"
#elif defined OS_MACOS
#define pwrv_cOpSys "Mac OS"
#elif defined OS_FREEBSD
#define pwrv_cOpSys "FreeBSD"
#elif defined OS_OPENBSD
#define pwrv_cOpSys "OpenBSD"
#elif defined OS_CYGWIN
#define pwrv_cOpSys "Cygwin"
#else
#define pwrv_cOpSys "Unknown Operating System"
#endif

#if defined __powerpc__
#define pwrv_cHardware "PowerPC"
#elif defined __x86__
#define pwrv_cHardware "x86"
#else
#define pwrv_cHardware "Unknown Hardware"
#endif

#if (pwr_dHost_byteOrder == pwr_dLittleEndian)
#define SET_VERSION(a, b, c, d)                                                \
  ((pwr_tVersion)((d << 24) + (c << 16) + (b << 8) + a))
#else
#define SET_VERSION(a, b, c, d)                                                \
  ((pwr_tVersion)((a << 24) + (b << 16) + (c << 8) + d))
#endif

#define pwrv_cBuildTimeStr "08-OCT-2019 12:00:00"

#define pwrv_cPwrVersionStr "V5.7.0"
#define pwrv_cPwrVersion SET_VERSION('V', 5, 7, 0)

#define pwrv_cWbdbVersionShortStr "V57"
#define pwrv_cPwrCopyright "Copyright (C) 2005-2019 SSAB EMEA AB"

#if (pwr_dHost_byteOrder == pwr_dLittleEndian)
#define PWRV_VERSION_MAJOR (0xFF & (pwrv_cPwrVersion >> 8))
#define PWRV_VERSION_MINOR (0xFF & (pwrv_cPwrVersion >> 16))
#define PWRV_VERSION_RELEASE (0xFF & (pwrv_cPwrVersion >> 24))
#else
#define PWRV_VERSION_MAJOR (0xFF & (pwrv_cPwrVersion >> 16))
#define PWRV_VERSION_MINOR (0xFF & (pwrv_cPwrVersion >> 8))
#define PWRV_VERSION_RELEASE (0xFF & (pwrv_cPwrVersion >> 0))
#endif

#endif
