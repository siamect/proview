/* Local definitions */

#define SEM_NAME "/tmp/pwr_mh_sem_"
static sem_t* sem = (sem_t*)-1;

/* Local function prototypes */

static pwr_tStatus sendMessage(qcom_sQid*, mh_sHead*);

static char* SemName()
{
  static char id[32];
  static char* str = NULL;

  if (str == NULL) {
    if ((str = getenv(pwr_dEnvBusId)) == NULL)
      return NULL;
    sprintf(
        id, "%s%.*s", SEM_NAME, (int)(sizeof(id) - strlen(SEM_NAME) - 1), str);
    str = id;
  }
  return str;
}

static pwr_tUInt32 map()
{
  char* name;
  int oflags = 0;

  if (sem != (sem_t*)-1)
    return 1;
  if ((name = SemName()) == NULL)
    return 2;
  sem = posix_sem_open(name, oflags);
  if (sem == (sem_t*)-1) {
    errh_Error(
        "rt_mh_utl: map. sem_open(\"%s\") failed, %m", name, errno_GetStatus());
    return 2;
  }
  return 1;
}

static pwr_tUInt32 unmap()
{
  if (sem == (sem_t*)-1)
    return 1;
  if (posix_sem_close(sem) == -1) {
    perror("rt_mh_utl: unmap. sem_close failed, ");
    sem = (sem_t*)-1; /* If you can't close it, it cannot be valid" */
    return 2;
  }
  sem = (sem_t*)-1;

  return 1;
}

static pwr_tStatus sendMessage(qcom_sQid* target, mh_sHead* head)
{
  pwr_tStatus sts;
  qcom_sPut put;

  put.data = (char*)head;
  put.type.b = mh_cMsgClass;
  put.type.s = 0;
  put.reply.qix = 0;
  put.reply.nid = 0;
  put.size = sizeof(*head);
  put.allocate = 1;

  qcom_Put(&sts, target, &put);

  return sts;
}

/* Create the event semaphore.  */

pwr_tStatus mh_UtilCreateEvent()
{
  char* name;
  int oflags = O_CREAT | O_EXCL;
  mode_t mode = S_IRWXU | S_IRWXG;

  if (sem != (sem_t*)-1)
    return 2;

  name = SemName();
  if (name == NULL)
    return 2;

  posix_sem_unlink(name); /* Don't care about status */
  sem = posix_sem_open(name, oflags, mode, 0);

  if (sem == (sem_t*)-1)
    return 2;

  posix_sem_close(sem);

  sem = (sem_t*)-1;

  return 1;
}

/*  Destroy the event semaphore.  */

pwr_tStatus mh_UtilDestroyEvent()
{
  char* name = SemName();

  if (name == NULL)
    return 2;

  if (sem != (sem_t*)-1)
  posix_sem_close(sem);
  sem = (sem_t*)-1;

  if (posix_sem_unlink(name) == -1) {
    perror("mh_UtilDestroyEvent: sem_unlink");
    return 2;
  }
  return 1;
}
/* Is the event monitor to started. */

pwr_tBoolean mh_UtilIsStartedMh()
{
  pwr_tStatus sts;

  sts = map();

  if (EVEN(sts))
    return FALSE;
  sts = posix_sem_trywait(sem);
  if (sts == 0) {
    posix_sem_post(sem);
    unmap();
  }

  return (sts == 0);
}

/* Inform Handler on local node that the list with supevise objects should be
   rebuilt and the scanning of supervise object restarted.

   Process must have done qcom_Init. */

pwr_tStatus mh_UtilStartScanSup(qcom_sQid source)
{
  pwr_tStatus sts;
  mh_sHead head;
  qcom_sQid target;

  head.xdr = FALSE;
  head.ver = mh_cVersion;
  sts = net_GetTime(&head.birthTime);
  head.qid = source;
  co_GetOwnPlatform(&head.platform);
  head.source = mh_eSource_Pcm;
  head.type = mh_eMsg_StartScanSup;

  /* Send message to local handler */

  target.nid = 0;
  target.qix = mh_cProcHandler;

  sts = sendMessage(&target, &head);

  return sts;
}

/* Inform Handler on local node that scanning of
   supervise object should be stopped.

   Process must have done qcom_Init. */

pwr_tStatus mh_UtilStopScanSup(qcom_sQid source)
{
  pwr_tStatus sts;
  mh_sHead head;
  qcom_sQid target;

  head.xdr = FALSE;
  head.ver = mh_cVersion;
  sts = net_GetTime(&head.birthTime);
  head.qid = source;
  co_GetOwnPlatform(&head.platform);
  head.source = mh_eSource_Pcm;
  head.type = mh_eMsg_StopScanSup;

  /* Send message to local handler */

  target.nid = 0;
  target.qix = mh_cProcHandler;

  sts = sendMessage(&target, &head);

  return sts;
}

/*  Wait for the Plc to start. */

pwr_tStatus mh_UtilWaitForMh()
{
  pwr_tStatus sts;

  sts = map();

  if (EVEN(sts))
    return sts;

  posix_sem_wait(sem);
  posix_sem_post(sem);
  unmap();

  return 1;
}

/* Wake all waiting for the event monitor to start. */

pwr_tStatus mh_UtilWake()
{
  pwr_tStatus sts;

  sts = map();
  if (EVEN(sts))
    return (sts);

  posix_sem_post(sem);
  unmap();

  return 1;
}
