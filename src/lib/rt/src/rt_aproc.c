#include "co_cdh.h"
#include "co_time.h"
#include "rt_gdh.h"
#include "rt_aproc.h"
#include "rt_proc_msg.h"

static pwr_sNode* proc_np = 0;

/**
 * @brief Register Application object.
 * The Application object is stored in the $Node object and be opened from the
 * node graph.
 *
 *    \param oid	Objid for Application object.
 *    \return           Status or operation.
 */
pwr_tStatus aproc_RegisterObject(pwr_tOid oid)
{
  pwr_tStatus sts;
  errh_eAnix anix = errh_Anix();

  if (!anix)
    return PROC__ANIX;

  if (!proc_np) {
    pwr_tOid noid;

    sts = gdh_GetNodeObject(0, &noid);
    if (EVEN(sts))
      return sts;

    sts = gdh_ObjidToPointer(noid, (void**)&proc_np);
    if (EVEN(sts))
      return sts;
  }
  proc_np->ProcObject[anix - 1] = oid;
  return PROC__SUCCESS;
}

/**
 * @brief Set application timestamp.
 * The timestamp is supervised by the event monitor and if the timestamp is
 * delayed more
 * than the max delay, timeout status is set for the process.
 *
 *    \param cycletime	Application cycle time in seconds.
 *    \param maxdelay	Max delay in seconds.
 *    \return           Status or operation.
 */
pwr_tStatus aproc_TimeStamp(float cycletime, float maxdelay)
{
  pwr_tStatus sts;
  pwr_tTime t, tmo_time;
  pwr_tDeltaTime dt;
  errh_eAnix anix = errh_Anix();

  if (!anix)
    return PROC__ANIX;

  if (!proc_np) {
    pwr_tOid noid;

    sts = gdh_GetNodeObject(0, &noid);
    if (EVEN(sts))
      return sts;

    sts = gdh_ObjidToPointer(noid, (void**)&proc_np);
    if (EVEN(sts))
      return sts;
  }

  time_GetTime(&t);
  time_FloatToD(&dt, cycletime + maxdelay);
  time_Aadd(&tmo_time, &t, &dt);
  proc_np->ProcTimeStamp[anix - 1] = tmo_time;
  return PROC__SUCCESS;
}
