typedef struct {
  pwr_tTime CreationTime;  
  pwr_tTime OpenTime;  
  pwr_tTime CloseTime;  
  pwr_tTime PutTime;  
  pwr_tTime GetTime;  
  pwr_tUInt32 Version;
  long PutCount;
  long GetCount;
  long FileSize;
  long FreeSize;
  long MaxFreeSize;
  long SegSize;
  long MaxSegSize;
  long SectSize;
  long MaxSectSize;
  long SectPos;
} mh_sBlockDbHead;

typedef struct {
  FILE *File;
  mh_sBlockDbHead Head;
} mh_sBlockDb;

