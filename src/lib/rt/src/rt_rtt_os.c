/*************************************************************************
*
* Name:		rtt_replace_env()
*
* Type		int
*
* Type		Parameter	IOGF	Description
*
* Description:
*	Replace env variables in UNIX filenames.
*	A string that starts with $ and ends with . or / will replaced
*	if then string is found by getenv. Only lower case variables
*	will be detected.
*
**************************************************************************/
int rtt_replace_env(char* str, char* newstr)
{
  char* s;
  char* t;
  char* u = NULL;
  int symbolmode;
  long int size;
  char* value;
  char symbol[80];
  char lower_symbol[80];
  char new[160];

  symbolmode = 0;
  s = str;
  t = new;

  while (*s != 0) {
    if (*s == '$') {
      symbolmode = 1;
      u = s + 1;
      *t = *s;
      t++;
    } else if (symbolmode && (*s == '/' || *s == '.')) {
      /* End of potential symbol */
      size = (long int)s - (long int)u;
      strncpy(symbol, u, size);
      symbol[size] = 0;
      cdh_ToLower(lower_symbol, symbol);
      if ((value = getenv(lower_symbol)) == NULL) {
        /* It was no symbol */
        *t = *s;
        t++;
      } else {
        /* Symbol found */
        t -= strlen(symbol) + 1;
        strcpy(t, value);
        t += strlen(value);
        *t = *s;
        t++;
      }
      symbolmode = 0;
    } else {
      *t = *s;
      t++;
    }
    s++;
  }

  if (symbolmode) {
    strcpy(t, u);
  } else
    *t = 0;

  strcpy(newstr, new);
  return 1;
}
#endif
