/*************************************************************************
*
* Name:		rtt_get_nodename()
*
* Type		int
*
* Type		Parameter	IOGF	Description
*
* Description:
*	Get nodename for the current system.
*
**************************************************************************/
int rtt_get_nodename(char* nodename, int size)
{
#if defined OS_POSIX
  struct utsname buf;

  if (uname(&buf) == -1)
    return 0;
  strncpy(nodename, buf.nodename, size);
  return 1;
#endif
}

