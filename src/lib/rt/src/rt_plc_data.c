/*_*
  @aref dpcollect DpCollect
*/
void DpCollect_exec(plc_sThread* tp, pwr_sClass_DpCollect* object)
{
  int i;
  pwr_tBoolean** InP;

  for (i = 0; i < object->MaxIndex; i++) {
    InP = (pwr_tBoolean**)((char*)&object->DpIn1P + i * pwr_cInputOffset);
    object->Dp[i] = **InP;
  }
}

