/*_*
  @aref dpdistribute DpDistribute
*/
void DpDistribute_exec(plc_sThread* tp, pwr_sClass_DpDistribute* object)
{
  if (*object->DataInP)
    memcpy(&object->DpOut1, *object->DataInP,
        MIN(object->MaxIndex, 24) * sizeof(pwr_tBoolean));
}

