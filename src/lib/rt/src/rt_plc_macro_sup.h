/*_*
  Name:
    DSup_exec(object, In, con)

  Description:
    Supervise digital parameter

  @aref dsup DSup
*/
#define DSup_exec(o, In, con)                                                  \
  timer2_scan(tp, o);                                                          \
  if ((In) != o->CtrlPosition) {					\
    if (o->Action)                                                             \
