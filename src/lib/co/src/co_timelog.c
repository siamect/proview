#include <stdio.h>
#include <string.h>
#include <stdlib.h>
#include "co_timelog.h"
#include "co_time.h"

/* 1 redcom connect */
/* 2 redcom tables */
/* 4 alarms and events */
unsigned int tmlog = 0;

void tm_log(unsigned int level, const char* func, const char* str)
{
  char logstr[200];
  int i;
  time_AtoAscii(NULL, time_eFormat_DateAndTime, logstr, sizeof(logstr));
  strcat(logstr, " ");
  strcat(logstr, func);
  for (i = strlen(func); i < 25; i++)
    strcat(logstr, " ");
  strcat(logstr, str);
  printf("%s\n", logstr);
}

