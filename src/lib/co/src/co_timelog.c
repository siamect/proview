/* 1 redcom connect */
/* 2 redcom tables */
/* 4 alarms and events */
unsigned int tmlog = 1;

void tm_log( unsigned int level, const char *func, const char *str)
{
  char logstr[200];
  int i;
  time_AtoAscii( NULL, time_eFormat_DateAndTime, logstr, sizeof(logstr));
  strcat( logstr, " ");
  strcat( logstr, func);
  for ( i = strlen(func); i < 25; i++)
    strcat( logstr, " ");
  strcat( logstr, str);
  printf( "%s\n", logstr);
}

