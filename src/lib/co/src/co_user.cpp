SystemList* GeUser::find_system(SystemName* name)
{
  SystemList* sl;

  for (sl = root; sl; sl = sl->next) {
    if (cdh_NoCaseStrcmp(sl->name, name->segment(0)) == 0) {
      if (name->segments == sl->level + 1)
        return sl;
      return sl->find_system(name);
    }
  }
  return NULL;
}

