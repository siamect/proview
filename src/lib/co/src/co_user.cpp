SystemList* GeUser::get_system(UserList* user)
{
  SystemList* sl = root;
  while (sl) {
    UserList* ul = sl->userlist;
    while (ul) {
      if (ul == user)
        return sl;
      ul = ul->next;
    }
    SystemList* found_sl = get_system_child(sl, user);
    if (found_sl)
      return found_sl;
    sl = sl->next;
  }

  return 0;
}

SystemList* GeUser::get_system_child(SystemList* system, UserList* user)
{
  SystemList* sl = system->childlist;
  while (sl) {
    UserList* ul = sl->userlist;
    while (ul) {
      if (ul == user)
        return sl;
      ul = ul->next;
    }
    SystemList* found_sl = get_system_child(sl, user);
    if (found_sl)
      return found_sl;
    sl = sl->next;
  }

  return 0;
}

bool GeUser::get_system_name(SystemList* system, char* name)
{
  SystemList* sl = root;
  while (sl) {
    if (sl == system) {
      strcpy(name, sl->name);
      return true;
    }
    if (get_system_name_child(sl, system, name)) {
      char tmp[256];
      strcpy(tmp, name);
      strcpy(name, sl->name);
      strcat(name, ".");
      strcat(name, tmp);
      return true;
    }
    sl = sl->next;
  }
  return false;
}

