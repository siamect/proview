// This file contains unique_ptr from C++11 backported to C++03.

#ifndef CO_UNIQUE_PTR
#define CO_UNIQUE_PTR

#include <stddef.h>

// std::unique_ptr for single objects -> free memory with 'delete'
template<typename T>
class unique_ptr {
public:
    unique_ptr(T* p) : value(p) {
    }
    unique_ptr(unique_ptr&& x) : value(x.release()) {
    }

    unique_ptr& operator=(unique_ptr&& x) {
        reset(x.release());
        return *this;
    }

    ~unique_ptr() {
        reset();
    }

    void reset(T* p = NULL) {
        if (p != value) {
            delete value;
            value = p;
        }
    }

    T* release() {
        T* const tmp = value;
        value = NULL;
        return tmp;
    }

    T& operator*() const {
        return *value;
    }

    T* operator->() const {
        return value;
    }

    T* get() const {
        return value;
    }
protected:
    T* value;

    unique_ptr(const unique_ptr&);
    unique_ptr& operator=(const unique_ptr&);
    unique_ptr& operator=(T* p);
};

// std::unique_ptr for arrays -> free memory with 'delete[]'
template<typename T>
class unique_ptr<T[]> {
public:
    unique_ptr(T* p) : value(p) {
    }
    unique_ptr(unique_ptr&& x) : value(x.release()) {
    }

    unique_ptr& operator=(unique_ptr&& x) {
        reset(x.release());
        return *this;
    }

    ~unique_ptr() {
        reset();
    }

    void reset(T* p = NULL) {
        if (p != value) {
            delete[] value;
            value = p;
        }
    }

    T* release() {
        T* const tmp = value;
        value = NULL;
        return tmp;
    }

    T& operator[](int i) const {
        return value[i];
    }

    T* get() const {
        return value;
    }
protected:
    T* value;

    unique_ptr(const unique_ptr&);
    unique_ptr& operator=(const unique_ptr&);
    unique_ptr& operator=(T* p);
};

#endif
