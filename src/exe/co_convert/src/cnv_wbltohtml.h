#ifndef cnv_wbltohtml_h
#define cnv_wbltohtml_h

#include <vector>

#include "cnv_wblto.h"

class CnvReadWbl;

class cnv_mentry {
  public:
  cnv_mentry()
  {
  }
  bool operator<(const cnv_mentry& x)
  {
    return (strcmp(name, x.name) < 0);
  }
  char name[32];
  pwr_tFileName file;
};

class CnvWblToHtml : public CnvWblTo {
  public:
  CnvWblToHtml(CnvCtx* cnv_ctx)
      : ctx(cnv_ctx), html_class_open(0), html_index_open(0), js_all_first(0),
        cdp_created(false)
  {
  }
  virtual ~CnvWblToHtml()
  {
  }

  CnvCtx* ctx;
  std::ofstream fp_html_clf;
  std::ofstream fp_html_index;
  std::ofstream fp_js_all;
  std::ofstream fp_tmp;
  std::ofstream fp_html_group[MAX_GROUPS];
  std::ofstream fp_js_group[MAX_GROUPS];
  char html_first[80];
  int html_class_open;
  int html_index_open;
  bool js_all_first;
  bool js_group_first[80];
  bool cdp_created;
  std::vector<cnv_mentry> all_types;
  std::vector<cnv_mentry> all_classes;

  int init(char* first);
  int close();
  int class_exec();
  int class_close();
  int body_exec();
  int body_close();
  int attribute_exec();
  int graphplcnode();
  int graphplccon();
  int template_exec();
  int typedef_exec();
  int typedef_close();
  int bit_exec();
  Cnv_eWblToType type()
  {
    return Cnv_eWblToType_Html;
  }
  int class_open()
  {
    return fp_html_clf.is_open();
  }
  int index_open()
  {
    return fp_html_index.is_open();
  }
  void create_cdp_file(const char* volume_name, const char* class_name,
      const char* attr_typeref);
  void print_all_menu();
};

#endif
