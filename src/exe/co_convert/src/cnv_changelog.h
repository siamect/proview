#ifndef cnv_changelog_h
#define cnv_changelog_h

/* cnv_changelog.h -- Read changelog files */

#include "cnv_ctx.h"

class LogEntry {
  public:
  LogEntry()
  {
    strcpy(signature, "");
    strcpy(module, "");
    strcpy(component, "");
    strcpy(text, "");
  }
  LogEntry(const LogEntry& x) : time(x.time)
  {
    strcpy(signature, x.signature);
    strcpy(module, x.module);
    strcpy(component, x.component);
    strcpy(text, x.text);
  }
  const LogEntry& operator=(const LogEntry& x)
  {
    time = x.time;
    strcpy(signature, x.signature);
    strcpy(module, x.module);
    strcpy(component, x.component);
    strcpy(text, x.text);
    return *this;
  }
  pwr_tTime time;
  char signature[10];
  char module[40];
  char component[40];
  char text[400];
};

class CnvChangeLog {
  public:
  std::vector<LogEntry> entries;
  CnvCtx* ctx;
  int from;
  pwr_tTime from_time;

  CnvChangeLog(CnvCtx* cnv_ctx, char* from_str);

  int read(const char* module);
  void print();
  void print_docbook();
  void print_html();
  void sort_time();
  void sort_module();
  void sort_component();
  void sort_signature();
  static void from_git();
};

#endif
