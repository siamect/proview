char* CnvPwgToXtthelp::topic_name(char* str)
{
  static char res[200];
  char *s, *t;

  // Replace '/' to '_'
  for (s = str, t = res; *s; s++, t++) {
    if (*s == '/')
      *t = '_';
    else
      *t = *s;
  }
  *t = 0;
  return res;
}
