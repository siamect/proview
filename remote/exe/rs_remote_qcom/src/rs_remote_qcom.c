#define TIME_INCR 0.02
#define debug 0
#define remote_cMsgClass 204

static remnode_item rn;
static pwr_sClass_RemnodeQCom* rn_qcom;
static qcom_sQid local_qid;
static qcom_sQid target_qid;

/*************************************************************************
**************************************************************************
*
* RemoteSleep
*
**************************************************************************
**************************************************************************/

void RemoteSleep(float time)
{
  struct timespec rqtp, rmtp;

  rqtp.tv_sec = 0;
  rqtp.tv_nsec = (long int)(time * 1000000000);
  nanosleep(&rqtp, &rmtp);
  return;
}

/*************************************************************************
**************************************************************************
*
* Namn : qcom_receive
*
* Typ  : unsigned int
*
* Typ		Parameter	       IOGF	Beskrivning
*
* Beskrivning : Invoked when a MQ message is received.
*
**************************************************************************
**************************************************************************/

unsigned int qcom_receive()
{
  pwr_tStatus sts;
  char search_remtrans;
  remtrans_item* remtrans;
  unsigned int timeout;
  qcom_sGet get;

  memset(&get, 0, sizeof(get));
  get.maxSize = 32000;
  timeout = (unsigned int)(TIME_INCR * 1000);

  qcom_Get(&sts, &local_qid, &get, timeout);
  if (sts == QCOM__TMO || sts == QCOM__QEMPTY)
    return sts;

  if (debug)
    printf("Received message %d\n", get.size);

  switch ((int)get.type.b) {
  case remote_cMsgClass: {
    search_remtrans = true;

    remtrans = rn.remtrans;
    while (remtrans && search_remtrans) {
      if (remtrans->objp->Address[0] == get.type.s
          && remtrans->objp->Direction == REMTRANS_IN) {
        search_remtrans = false;
        sts = RemTrans_Receive(remtrans, (char*)get.data, get.size);
        if (sts != STATUS_OK && sts != STATUS_BUFF)
          errh_Error("Error from RemTrans_Receive, queue %d, status %d",
              rn_qcom->ReceiveQueue, sts, 0);
        break;
      }
      remtrans = (remtrans_item*)remtrans->next;
    }
    if (search_remtrans) {
      rn_qcom->ErrCount++;
      errh_Info("No remtrans for received message, queue %d, class ",
          rn_qcom->ReceiveQueue, get.type.s, 0);
    }

    if (get.data)
      qcom_Free(&sts, get.data);
    break;
  }
  default:
    errh_Error("qcom_Get, unknown message type %d received", get.type.s);
  }
  return sts;
}

/*************************************************************************
**************************************************************************
*
* Namn : qcom_send
*
* Typ  : unsigned int
*
* Typ		Parameter	       IOGF	Beskrivning
*
* Beskrivning : Sends a MQ message to Remote node
*
**************************************************************************
**************************************************************************/

unsigned int qcom_send(remnode_item* remnode, pwr_sClass_RemTrans* remtrans,
    char* buf, int buf_size)

{
  pwr_tStatus sts;
  qcom_sPut put;

  target_qid.qix = rn_qcom->TargetQueue;

  memset(&put, 0, sizeof(put));
  put.data = buf;
  put.allocate = 1;
  put.size = buf_size;
  put.type.b = remote_cMsgClass;
  put.type.s = remtrans->Address[0];
  put.reply.qix = 0;
  put.reply.nid = 0;

  if (remtrans->ReplacePrev)
    put.msg_id = remtrans->Address[0] + 1;

  qcom_Put(&sts, &target_qid, &put);
  if (EVEN(sts)) {
    remtrans->ErrCount++;
    errh_Error(
        "Send failed, queue %d, QCom status %d", rn_qcom->ReceiveQueue, sts, 0);
    if (debug)
      printf("Send failed sts:%d\n", (int)sts);
  }

  return (STATUS_OK);
}

/*************************************************************************
**************************************************************************
*
* Main
*
**************************************************************************
**************************************************************************/

int main(int argc, char* argv[])
{
  remtrans_item* remtrans;
  char id[32];
  char pname[45];
  pwr_tStatus sts;
  int i;
  float time_since_scan = 0.0;
  char queue_name[80];
  qcom_sQattr qattr;
  qcom_sNode node;
  pwr_tNid nid;
  int found = 0;

  /* Read arg number 2, should be id for this instance and id is our queue
   * number */

  if (argc >= 2)
    strncpy(id, argv[1], sizeof(id));
  else
    strcpy(id, "0");

  /* Build process name with id */

  sprintf(pname, "rs_remqcom_%s", id);

  /* Init of errh */

  errh_Init(pname, errh_eAnix_remote);
  errh_SetStatus(PWR__SRVSTARTUP);

  /* Init of gdh */
  if (debug)
    printf("Before gdh_init\n");
  sts = gdh_Init(pname);
  if (EVEN(sts)) {
    errh_Fatal("gdh_Init, %m", sts);
    errh_SetStatus(PWR__SRVTERM);
    exit(sts);
  }

  /* Arg number 3 should be my remnodes objid in string representation,
     read it, convert to real objid and store in remnode_item */

  sts = 0;
  if (argc >= 3)
    sts = cdh_StringToObjid(argv[2], &rn.objid);
  if (EVEN(sts)) {
    errh_Fatal("cdh_StringToObjid, %m", sts);
    errh_SetStatus(PWR__SRVTERM);
    exit(sts);
  }

  /* Get pointer to RemnodeQCom object and store locally */

  sts = gdh_ObjidToPointer(rn.objid, (pwr_tAddress*)&rn_qcom);
  if (EVEN(sts)) {
    errh_Fatal("cdh_ObjidToPointer, %m", sts);
    errh_SetStatus(PWR__SRVTERM);
    exit(sts);
  }

  if (!rn_qcom->ReceiveQueue && !rn_qcom->TargetQueue) {
    errh_Fatal(
        "Process terminated, neither send or receive queue configured, %s", id);
    errh_SetStatus(PWR__SRVTERM);
    exit(sts);
  }

  /* Initialize some internal data and make standard remtrans init */

  rn.next = NULL;
  rn.local = NULL; // We dont use local structure since we only have one remnode
  rn_qcom->ErrCount = 0;
  if (debug)
    printf("Before remtrans_init\n");

  sts = RemTrans_Init(&rn);

  if (EVEN(sts)) {
    errh_Fatal("RemTrans_Init, %m", sts);
    errh_SetStatus(PWR__SRVTERM);
    exit(sts);
  }

  /* Store remtrans objects objid in remnode_qcom object */
  remtrans = rn.remtrans;
  i = 0;
  while (remtrans) {
    rn_qcom->RemTransObjects[i++] = remtrans->objid;
    if (i >= (int)(sizeof(rn_qcom->RemTransObjects)
                 / sizeof(rn_qcom->RemTransObjects[0])))
      break;
    remtrans = (remtrans_item*)remtrans->next;
  }

  /* Create queue */
  if (rn_qcom->ReceiveQueue) {
    qattr.type = qcom_eQtype_private;
    qattr.quota = 100;

    local_qid.nid = 0;
    local_qid.qix = rn_qcom->ReceiveQueue;

    sprintf(queue_name, "RemQue%d", rn_qcom->ReceiveQueue);

    qcom_CreateQ(&sts, &local_qid, &qattr, queue_name);
    if (sts == QCOM__QALLREXIST) {
      qcom_DeleteQ(&sts, &local_qid);
      qcom_CreateQ(&sts, &local_qid, &qattr, queue_name);
    }
    if (EVEN(sts)) {
      errh_Fatal("Process terminated, QCom create queue, %d", sts);
      errh_SetStatus(PWR__SRVTERM);
      exit(sts);
    }
  }

  /* Get remote queue */
  if (rn_qcom->TargetQueue != 0) {
    qcom_MyNode(&sts, &node);
    if (str_NoCaseStrcmp(node.name, rn_qcom->TargetNode) == 0) {
      found = 1;
      nid = node.nid;
    } else {
      for (nid = qcom_cNNid; qcom_NextNode(&sts, &node, nid); nid = node.nid) {
        if (str_NoCaseStrcmp(node.name, rn_qcom->TargetNode) == 0) {
          found = 1;
          break;
        }
      }
    }
    if (!found) {
      errh_Fatal(
          "Process terminated, target node not found, %s", rn_qcom->TargetNode);
      errh_SetStatus(PWR__SRVTERM);
      exit(sts);
    }

    target_qid.nid = nid;
    target_qid.qix = rn_qcom->TargetQueue;
  }

  /* Set running status */

  errh_SetStatus(PWR__SRUN);

  /* Set (re)start time in remnode object */

  time_GetTime(&rn_qcom->RestartTime);

  /* Loop forever */

  while (!doomsday) {
    if (rn_qcom->Disable == 1) {
      errh_Fatal("Disabled, exiting");
      errh_SetStatus(PWR__SRVTERM);
      exit(0);
    }
    aproc_TimeStamp(TIME_INCR, 5);

    if (rn_qcom->ReceiveQueue)
      sts = qcom_receive();
    else
      RemoteSleep(TIME_INCR);

    time_since_scan += TIME_INCR;
    if (time_since_scan >= rn_qcom->ScanTime) {
      if (rn_qcom->TargetQueue)
        sts = RemTrans_Cyclic(&rn, &qcom_send);
      time_since_scan = 0.0;
    }
  }
}
