#include "co_time.h"
#include "rt_qcom.h"
#include "pwr_remoteclasses.h"
#include "remote.h"

pwr_tStatus remote_logg(int identity, char* str, int size)
{
  qcom_sPut put;
  qcom_sQid qid;
  char* logmsg;
  char timstr[26];
  int logsize = size + 5 + sizeof(timstr);
  pwr_tStatus sts;

  time_AtoAscii(0, time_eFormat_DateAndTime, timstr, sizeof(timstr));

  logmsg = malloc(logsize);
  *(int*)logmsg = identity;
  strcpy(&logmsg[4], timstr);
  strcat(&logmsg[4], "  ");
  strncat(&logmsg[4], str, size + 1);

  put.data = logmsg;
  put.allocate = 1;
  put.size = logsize;
  put.type.b = qcom_eBtype__;
  put.type.s = qcom_eStype__;
  put.reply.qix = 0;
  put.reply.nid = 0;

  qid = qcom_cNQid;
  qid.qix = rs_pwr_logg_qix;

  sts = qcom_Put(&sts, &qid, &put);

  free(logmsg);

  return sts;
}
